# 2) კომენტარების გამოყენებით ახსენი display: inline;

# display: inline; CSS-ის თვისებაა, რომელიც ელემენტს inline დონეზე აყენებს.
# ეს ნიშნავს, რომ ელემენტი არ იწყება ახალი ხაზით და იკავებს მხოლოდ იმ სივრცეს, რაც მისი შინაარსისთვის არის საჭირო.
# inline ელემენტები ჩვეულებრივ გამოიყენება ტექსტის ფორმატირებისათვის და გვერდზე ერთმანეთის გვერდით განლაგებისათვის.