#დავალება 2: წონის შეფასება ასაკის მიხედვით (უბრალოდ და ლოგიკურად)


#  აღწერა:
# მომხმარებელი შეიყვანს საკუთარ ასაკს და წონას.
# პროგრამამ უნდა შეაფასოს წონა ასაკის მიხედვით მარტივი ლოგიკით:

# თუ ასაკი < 10:

# წონა < 20 → "წონა დაბალია"

# წონა 20-40 → "წონა ნორმალურია"

# წონა > 40 → "წონა მაღალია"

# თუ ასაკი 10-17:

# წონა < 40 → "წონა დაბალია"

# წონა 40-65 → "წონა ნორმალურია"

# წონა > 65 → "წონა მაღალია"

# თუ ასაკი 18 ან მეტი:

# წონა < 50 → "წონა დაბალია"

# წონა 50-90 → "წონა ნორმალურია"

# წონა > 90 → "წონა მაღალია"



# მომხმარებლისგან ასაკისა და წონის მიღება
age = int(input("შეიყვანეთ თქვენი ასაკი: "))
weight = int(input("შეიყვანეთ თქვენი წონა: "))

# ასაკისა და წონის მიხედვით წონის შეფასება
if age < 10:
    if weight < 20:
        print("წონა დაბალია")
    elif 20 <= weight <= 40:
        print("წონა ნორმალურია")
    else:
        print("წონა მაღალია")
elif 10 <= age <= 17:
    if weight < 40:
        print("წონა დაბალია")
    elif 40 <= weight <= 65:
        print("წონა ნორმალურია")
    else:
        print("წონა მაღალია")
else:  # age >= 18
    if weight < 50:
        print("წონა დაბალია")
    elif 50 <= weight <= 90:
        print("წონა ნორმალურია")
    else:
        print("წონა მაღალია")