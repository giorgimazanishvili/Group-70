# 3) უკვე შექმნილი Dictionary-დან წამოიღეთ მხოლოდ key-ები და ასევე value.

# უკვე არსებული Dictionary
my_dict = {
    "name": "Ana",
    "age": 25,
    "city": "Tbilisi"
}

# მხოლოდ key-ების წამოღება
keys = my_dict.keys()
print("Keys:", keys)

# მხოლოდ value-ების წამოღება
values = my_dict.values()
print("Values:", values)