# 1) კომენტარებით ახსენით რისთვის გამოიყენება Len()-ფუნქცია.

# len() არის ჩაშენებული ფუნქცია პითონში,
# რომელიც გამოიყენება იმისთვის, რომ გავიგოთ ელემენტების რაოდენობა
# მოცემულ ობიექტში — მაგალითად, სიაში, სტრიქონში, ტუპლში და ა.შ.

# მაგალითი:
colors = ["წითელი", "ლურჯი", "მწვანე"]  # ეს არის ფერების სია, რომელშიც არის 3 ელემენტი

# len(colors) დააბრუნებს ამ სიის სიგრძეს ანუ ელემენტების რაოდენობას
length = len(colors)

# შედეგის გამოპრინტვა
print("სიის სიგრძეა:", length)  # გამოიტანს: სიის სიგრძეა: 3