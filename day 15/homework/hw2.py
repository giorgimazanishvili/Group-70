# 2) კომენტარებით ახსენით რისთვის გამოიყენება List-ი.

# List (სია) არის მონაცემთა სტრუქტურა Python-ში,
# რომელიც გამოიყენება მრავალი ელემენტის შესანახად ერთ ცვლადში.

# სია შეიძლება შეიცავდეს:
# - ტექსტებს (სტრიქონებს)
# - რიცხვებს
# - სხვა სიებსაც კი
# და შესაძლებელია სხვადასხვა ტიპის ელემენტების შერევაც ერთ სიაში.

# მაგალითი: ვქმნით ფერების სიას
colors = ["წითელი", "ლურჯი", "მწვანე"]

# სიის თითოეულ ელემენტს აქვს ინდექსი (ნომერი), რომელიც იწყება 0-დან:
# colors[0] → "წითელი"
# colors[1] → "ლურჯი"
# colors[2] → "მწვანე"

# სია საშუალებას გვაძლევს:
# → დავამატოთ ელემენტები
colors.append("ყვითელი")  # სიაში დაამატებს ახალ ფერს

# → წავშალოთ ელემენტები
colors.remove("ლურჯი")  # სიიდან წაშლის "ლურჯი"-ს

# → ვიპოვოთ სიის სიგრძე
length = len(colors)

# → მივწვდეთ კონკრეტულ ელემენტს ინდექსით
first_color = colors[0]

# შედეგების დაბეჭდვა
print("ფერების სია:", colors)
print("სიის სიგრძეა:", length)
print("პირველი ფერია:", first_color)