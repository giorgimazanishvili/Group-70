# 3) კომენტარებით ახსენით რა განსხვავებაა List-სა და ცვლადებ-ს შორის.

# ცვლადი (Variable) არის "კონტეინერი", რომელშიც ინახება ერთი მნიშვნელობა.

color = "წითელი"  # ეს არის ცვლადი, რომელიც შეიცავს მხოლოდ ერთ ფერს

# List (სია) კი არის ცვლადი, რომელიც ინახავს **მრავალ მნიშვნელობას ერთდროულად**.

colors = ["წითელი", "ლურჯი", "მწვანე"]  # ეს არის სია, ანუ ერთ ცვლადში რამდენიმე ფერია შენახული

# განსხვავება:

# 1. ჩვეულებრივი ცვლადი ინახავს მხოლოდ ერთ მონაცემს
print("ერთი ფერი:", color)  # შედეგი: წითელი

# 2. სია ინახავს რამდენიმე მონაცემს ერთ ცვლადში
print("მრავალი ფერი:", colors)  # შედეგი: ['წითელი', 'ლურჯი', 'მწვანე']

# 3. სიაში შეგვიძლია ელემენტების დამატება/წაშლა/ცვლილება
colors.append("ყვითელი")  # სიაში დაემატა ახალი ფერი
print("განახლებული სია:", colors)

# 4. ჩვეულებრივ ცვლადს არ შეგვიძლია ამგვარი მოქნილობა — მას მხოლოდ ერთი მნიშვნელობა აქვს.
