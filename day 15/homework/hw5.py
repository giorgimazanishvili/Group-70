# 5) შექმენი ლისტი, შეინახეთ სამი წიგნის სახელი. დაბეჭდე მთელი სია და დაბეჭდე, რამდენი წიგნია მასში.

# წიგნების სია
books = ["ჰარი პოტერი", "მარტინ იდენი", "ძმა კარამაზოვები"]

# მთლიანი სიის დაბეჭდვა
print("წიგნების სია:", ", ".join(books))

# ვგებულობთ რამდენი წიგნია სიაში
count = len(books)

# წიგნების რაოდენობის დაბეჭდვა
print("სიის სიგრძეა:", count)