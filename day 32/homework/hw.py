# Python კოდი:
A = {1, 2, 3, 4, 5}

# 1. ელემენტების რაოდენობა
print("Element count:", len(A))

# 2. არის თუ არა 3 სეტის ელემენტი
print("Is 3 in set A?", 3 in A)

# 3. დაამატე რიცხვი 6 ამ სეტში
