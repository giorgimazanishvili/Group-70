# 2) მომხმარებელს შემოატანინეთ ორი რიცხვითი მნიშვნელობა (სიგანე და სიმაღლე). ტერმინალში გამოიტანეთ ფართობი/პერიმეტრი. S = ფართობი (S = width*height)/P = პერიმეტრი (P = (width+height)*2

# მომხმარებლისგან სიგანის და სიმაღლის მიღება
width = float(input("გთხოვთ, შეიყვანოთ სიგანე: "))
height = float(input("გთხოვთ, შეიყვანოთ სიმაღლე: "))

# ფართობის და პერიმეტრის გამოთვლა
area = width * height
perimeter = 2 * (width + height)

# შედეგის დაბეჭდვა
print(f"Fartobi (S) = {area}, Perimetri (P) = {perimeter}")

# 3) კომენტარების სახით ახსენი რა არის input და output
# მომხმარებლისგან სიგანის და სიმაღლის მიღება
# input() არის ფუნქცია, რომელიც იღებს მომხმარებლის შეყვანას (ინპუტს).
# ის აძლევს საშუალებას მომხმარებელს, რომ ჩაწეროს გარკვეული მონაცემები, რომლებიც შემდგომ გამოიყენება პროგრამაში.

width = float(input("გთხოვთ, შეიყვანოთ სიგანე: "))  # აქ მომხმარებელი აწერს სიგანეს
height = float(input("გთხოვთ, შეიყვანოთ სიმაღლე: "))  # აქ მომხმარებელი აწერს სიმაღლეს

# ფართობის და პერიმეტრის გამოთვლა
area = width * height  # ფართობი ითვლება სიგანე * სიმაღლე
perimeter = 2 * (width + height)  # პერიმეტრი ითვლება 2 * (სიგანე + სიმაღლე)

# შედეგის დაბეჭდვა
# output() არის ფუნქცია, რომელიც გამოაქვს შედეგი ეკრანზე.
# print() ხელს უწყობს გამოყვანილ მონაცემთა (კერძოდ ამ შემთხვევაში ფართობისა და პერიმეტრის) ჩვენებას.

print(f"Fartobi (S) = {area}, Perimetri (P) = {perimeter}")  # აქ ვაწვდით მომხმარებელს შედეგს

