# 3) рЃарЃљрЃбрЃЮрЃЏ рЃБрЃюрЃЊрЃљ рЃЋрЃгрЃћрЃарЃЮрЃЌ Tag-рЃћрЃЉрЃў рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃўрЃЌ?.

# ­ЪДа рЃЏрЃўрЃќрЃћрЃќрЃћрЃЉрЃў, рЃЌрЃБ рЃарЃљрЃбрЃЮрЃЏ рЃЋрЃўрЃДрЃћрЃюрЃћрЃЉрЃЌ рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃА:
# 1№ИЈРЃБ рЃАрЃбрЃљрЃюрЃЊрЃљрЃарЃбрЃў рЃЊрЃљ рЃћрЃарЃЌрЃњрЃЋрЃљрЃарЃЮрЃЋрЃюрЃћрЃЉрЃљ (Consistency):
# рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃўрЃЌ рЃгрЃћрЃарЃљ рЃерЃћрЃћрЃАрЃљрЃЉрЃљрЃЏрЃћрЃЉрЃљ HTML рЃАрЃбрЃљрЃюрЃЊрЃљрЃарЃбрЃА (W3C).
# рЃћрЃА рЃћрЃ«рЃЏрЃљрЃарЃћрЃЉрЃљ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃАрЃбрЃћрЃЉрЃА рЃБрЃЎрЃћрЃЌ рЃгрЃљрЃЎрЃўрЃЌрЃ«рЃЋрЃљрЃерЃў рЃЊрЃљ рЃњрЃБрЃюрЃЊрЃБрЃарЃў рЃЎрЃЮрЃЊрЃўрЃА рЃАрЃгрЃЮрЃарЃў рЃЏрЃљрЃарЃЌрЃЋрЃљрЃерЃў.

# 2№ИЈРЃБ рЃЎрЃЮрЃЊрЃўрЃА рЃгрЃљрЃЎрЃўрЃЌрЃ«рЃЋрЃљрЃЊрЃЮрЃЉрЃљ:
# рЃДрЃЋрЃћрЃџрЃљ рЃбрЃћрЃњрЃў рЃЌрЃБ рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃўрЃЌрЃљрЃљ, рЃЎрЃЮрЃЊрЃў рЃБрЃцрЃарЃЮ рЃЏрЃЎрЃљрЃцрЃўрЃЮ рЃЊрЃљ рЃЏрЃљрЃарЃбрЃўрЃЋрЃљрЃЊ рЃњрЃљрЃАрЃљрЃњрЃћрЃЉрЃўрЃљ.

# 3№ИЈРЃБ рЃБрЃцрЃарЃЮ рЃюрЃљрЃЎрЃџрЃћрЃЉрЃў рЃерЃћрЃфрЃЊрЃЮрЃЏрЃўрЃА рЃарЃўрЃАрЃЎрЃў:
# рЃќрЃЮрЃњрЃўрЃћрЃарЃЌ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў (рЃњрЃљрЃюрЃАрЃљрЃЎрЃБрЃЌрЃарЃћрЃЉрЃўрЃЌ XHTML-рЃерЃў), рЃбрЃћрЃњрЃћрЃЉрЃўрЃА рЃЊрЃўрЃЊрЃў рЃљрЃАрЃЮрЃћрЃЉрЃўрЃЌ рЃгрЃћрЃарЃљ рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ рЃњрЃљрЃЏрЃЮрЃўрЃгрЃЋрЃўрЃЮрЃА рЃерЃћрЃфрЃЊрЃЮрЃЏрЃљ рЃљрЃю рЃњрЃљрЃБрЃерЃЋрЃљрЃА рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃљрЃЊ.

# 4№ИЈРЃБ рЃЎрЃЮрЃЊрЃћрЃарЃћрЃЉрЃўрЃА рЃЎрЃљрЃарЃњрЃў рЃЕрЃЋрЃћрЃЋрЃљ (Best Practice):
# рЃДрЃЋрЃћрЃџрЃљ рЃърЃарЃЮрЃцрЃћрЃАрЃўрЃЮрЃюрЃљрЃџрЃў рЃЊрЃћрЃЋрЃћрЃџрЃЮрЃърЃћрЃарЃў рЃўрЃДрЃћрЃюрЃћрЃЉрЃА рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃА рЃбрЃћрЃњрЃћрЃЉрЃерЃў. рЃћрЃА рЃљрЃарЃўрЃА рЃБрЃюрЃўрЃЋрЃћрЃарЃАрЃљрЃџрЃБрЃарЃў рЃгрЃћрЃАрЃў рЃърЃарЃљрЃЦрЃбрЃўрЃЎрЃљрЃерЃў.

