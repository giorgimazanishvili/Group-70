# 5) ახსენი რა განსხვავებაა ჩვეულებრივ ფუნქციასა და arrow function-ს შორის.

# ჩვეულებრივი ფუნქცია (function declaration) არის ფუნქციის განსაზღვრა, რომელიც იწყება სიტყვით "def" და აქვს თავისი სახელწოდება.

# მაგალითად:

def add(a, b):
    return a + b

# Arrow function (lambda function) არის ანონიმური ფუნქცია, რომელიც გამოიყენება ერთჯერადი ფუნქციური ოპერაციებისათვის და იწყება სიტყვით "lambda".

# მაგალითად:

add_lambda = lambda a, b: a + b