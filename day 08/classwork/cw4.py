# 4) მომხმარებელს შემოაყვანინეთ რიცხვი, სანამ ეს რიცხვი არ იქნება 100-ზე მეტი მაქამდე თავიდან მოთხოვთ რიცხვის შემოტანა.

# for loop-ით მომხმარებლის რიცხვის მოთხოვნა 100-ზე მეტი რიცხვის შესვლისთვის

for _ in iter(int, 1):  # თავი — უსასრულო იტერატორი (for loop-ის უსასრულო თავი)
    number = int(input("შეიყვანეთ რიცხვი, რომელიც უნდა იყოს 100-ზე მეტი: "))  # სხეული - რიცხვის მიღება
    if number > 100:  # სხეული - პირობის შემოწმება
        break  # გამოსვლა ციკლიდან, თუ რიცხვი > 100  