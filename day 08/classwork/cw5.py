# 5) მომხმარებელს შემოატანინეთ სხვადასხვა სახელები 5-ჯერ იტერაციის მეშვეობით, შექმენით ამ სახელებისთვის ცვლადი რომელშიც ყოველი სახელი დაემატება და ეს სახელები გამოიყოფა მძიმეებით, ანუ თქვენი ცვლადი 5 სახელის შეყვანის შემდეგ უნდა გამოიყურებოდეს ასე: "{სახელი პირველი}, {სახელი მეორე}, ... {სახელი მეხუთე}"

# 5) მომხმარებელს შევთხოვთ 5 სახელს და შევინახავთ ერთ სტრინგში მძიმეებით გამოყოფით

names = []  # სიას შევქმნით სახელების შესანახად

for i in range(5):
    name = input(f"შეიყვანეთ სახელი {i+1}: ")
    names.append(name)  # თითო სახელს ვუმატებთ სიას

# ყველა სახელი ვაერთიანებთ მძიმეებით გამოყოფით
result = ", ".join(names)

print("შეყვანილი სახელები:", result)