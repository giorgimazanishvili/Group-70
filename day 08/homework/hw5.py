# 5)და კომენტარებით ახსენი რა არის for loop.

# კარგი! აი, მაგალითი და კომენტარები, რომლებიც ახსნიან, რა არის `for` loop Python-ში:

# python :

# for loop — ეს არის ციკლი, რომელიც საშუალებას გვაძლევს შევასრულოთ ერთი და იგივე კოდი რამდენჯერმე
# უფრო კონკრეტულად, for loop ირჩევს თითოეულ ელემენტს იტერირებადი ობიექტიდან (მაგალითად, სიიდან ან ინტერვალიდან)
# და თითოეულ ელემენტზე ერთხელ ასრულებს ციკლის სხეულს.

for i in range(5):  # თავი — განსაზღვრავს რამდენჯერ უნდა შესრულდეს ციკლი (0-დან 4-მდე)
    print(f"მიმდინარე i-ის მნიშვნელობაა: {i}")  # სხეული — კოდი, რომელიც შესრულდება თითოეულ გამეორებაზე


# **მოკლედ:**
