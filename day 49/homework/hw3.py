# 3) კომენტარების სახით ახსენი transition

# ფუნქცია იღებს ორ პარამეტრს: მიმდინარე მდგომარეობას (state) და შეყვანას (input).
# ფუნქცია განსაზღვრავს ახალ მდგომარეობას არსებული მდგომარეობისა და შეყვანის მხოლოდ
# საფუძველზე.  transition: all 0.3s ease-in-out;  ეს CSS-ის თვისებაა, რომელიც
# საშუალებას აძლევს ელემენტებს ნაზად გადადიან ერთ მდგომარეობიდან მეორეში, როდესაც
# მათი სტილი იცვლება. მაგალითად, თუ ჩვენ გვაქვს ელემენტი, რომლის ფონი
# იცვლება, transition: all 0.3s ease-in-out; უზრუნველყოფს, რომ ეს ცვლილება
# 0.3 წამში მოხდება და ნაზად, არა კი მოულოდნელად.