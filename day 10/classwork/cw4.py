# 4) მომხმარებელს შემოატანინეთ ორი რიცხვი და დაბეჭდეთ უფრო მაღალი რიცხვი.

num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))

if num1 > num2:
    print(f"მეტია: {num1}")
elif num2 > num1:
    print(f"მეტია: {num2}")
else:
    print("ორივე რიცხვი ტოლია")