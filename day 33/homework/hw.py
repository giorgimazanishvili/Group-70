# 1)ახსენით რა ხდება მაშინ როდესაც ზრდით(padding-ს, border-ს და margin-ს).

# როდესაც padding-ს ზრდით, შიგნით არსებული შინაარსი უფრო შორს გადის კონტეინერის კიდეებისგან, რაც ქმნის დამატებით სივრცეს შიგნით.

# როდესაც border-ს ზრდით, კონტეინერის ჩარჩო ხდება უფრო ფართო და ვიზუალურად უფრო გამოხატული.

# როდესაც margin-ს ზრდით, კონტეინერები ერთმანეთისგან უფრო შორს გადიან, რაც ქმნის დამატებით სივრცეს მათ შორის.