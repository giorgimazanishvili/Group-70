# 6) ივარჯიშეთ შემდეგ ფუნქციებზე: int, str, float, bool. თითოეულზე გააკეთეთ 3-3 მაგალითი.

# სტრინგის გარდაქმნა მთელ რიცხვად
print(int("15"))        # შედეგი: 15

# რეალური რიცხვის გარდაქმნა მთელ რიცხვად
print(int(10.99))       # შედეგი: 10 (მრგვალდება ქვემოთ)

# ბულეს გარდაქმნა რიცხვად
print(int(True))        # შედეგი: 1 (True = 1, False = 0)