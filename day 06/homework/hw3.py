# 3) рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃўрЃЌ рЃљрЃ«рЃАрЃћрЃюрЃўрЃЌ, рЃарЃљ рЃљрЃарЃўрЃА рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃў рЃЊрЃљ рЃЕрЃљрЃЏрЃЮрЃЌрЃЋрЃљрЃџрЃћрЃЌ рЃарЃљ рЃњрЃќрЃћрЃЉрЃў рЃљрЃарЃАрЃћрЃЉрЃЮрЃЉрЃА рЃЏрЃўрЃА рЃгрЃљрЃарЃЏрЃЮрЃАрЃљрЃЊрЃњрЃћрЃюрЃљрЃЊ.

# рЃЏрЃЮрЃЊрЃўрЃЌ, рЃЏрЃљрЃарЃбрЃўрЃЋрЃљрЃЊ рЃЊрЃљ рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃўрЃЌ рЃљрЃЋрЃ«рЃАрЃюрЃљрЃЌ:

# ---

# ### ­Ъћ╣ 3) **рЃарЃљ рЃљрЃарЃўрЃА рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃў?**

# **рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃў** рЃљрЃарЃўрЃА рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃћрЃЉрЃўрЃА рЃЎрЃЮрЃюрЃЎрЃарЃћрЃбрЃБрЃџрЃў рЃЌрЃљрЃюрЃЏрЃўрЃЏрЃЊрЃћрЃЋрЃарЃЮрЃЉрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃЏрЃўрЃќрЃюрЃљрЃЊ рЃўрЃАрЃљрЃ«рЃљрЃЋрЃА рЃЎрЃЮрЃюрЃЎрЃарЃћрЃбрЃБрЃџрЃў рЃљрЃЏрЃЮрЃфрЃљрЃюрЃўрЃА рЃњрЃљрЃЊрЃљрЃгрЃДрЃЋрЃћрЃбрЃљрЃА.

# ­ЪЉЅ рЃАрЃ«рЃЋрЃљрЃњрЃЋрЃљрЃарЃљрЃЊ рЃарЃЮрЃЏ рЃЋрЃЌрЃЦрЃЋрЃљрЃЌ, рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃў рЃљрЃарЃўрЃА рЃюрЃљрЃЉрЃўрЃ»-рЃюрЃљрЃЉрЃўрЃ» рЃўрЃюрЃАрЃбрЃарЃБрЃЦрЃфрЃўрЃћрЃЉрЃў, рЃарЃЮрЃЏрЃџрЃћрЃЉрЃўрЃф рЃњрЃЋрЃћрЃ«рЃЏрЃљрЃарЃћрЃЉрЃљ рЃърЃарЃЮрЃЉрЃџрЃћрЃЏрЃўрЃА рЃњрЃљрЃЊрЃљрЃГрЃарЃљрЃерЃў.

