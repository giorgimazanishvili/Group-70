# ### РюЁ **рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў (рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃўрЃА рЃЉрЃБрЃюрЃћрЃЉрЃарЃўрЃЋрЃў рЃћрЃюрЃўрЃЌ рЃљрЃдрЃгрЃћрЃарЃљ):**

# **рЃљрЃЏрЃЮрЃфрЃљрЃюрЃљ:** рЃЕрЃљрЃўрЃЊрЃљрЃюрЃў рЃБрЃюрЃЊрЃљ рЃљрЃЋрЃљрЃЊрЃБрЃдрЃЮрЃЌ
# **рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃў:**

# 1. рЃљрЃўрЃдрЃћ рЃЕрЃљрЃўрЃЊрЃљрЃюрЃў
# 2. рЃЕрЃљрЃљрЃАрЃ«рЃў рЃгрЃДрЃљрЃџрЃў
# 3. рЃЊрЃљрЃЊрЃњрЃў рЃЦрЃБрЃарЃљрЃќрЃћ
# 4. рЃЕрЃљрЃарЃЌрЃћ рЃЦрЃБрЃарЃљ
# 5. рЃЊрЃљрЃћрЃџрЃЮрЃЊрЃћ рЃљрЃЊрЃБрЃдрЃћрЃЉрЃљрЃА
# 6. рЃЕрЃљрЃарЃЌрЃћ рЃЕрЃљрЃўрЃЊрЃюрЃўрЃА рЃњрЃљрЃЏрЃЮрЃарЃЌрЃЋрЃљ

# ---

# ­ЪДа **рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃўрЃА рЃгрЃљрЃарЃЏрЃЮрЃАрЃљрЃЊрЃњрЃћрЃюрЃў рЃњрЃќрЃћрЃЉрЃў**

# рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃў рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ рЃгрЃљрЃарЃЏрЃЮрЃЊрЃњрЃћрЃюрЃўрЃџрЃў рЃўрЃДрЃЮрЃА рЃАрЃ«рЃЋрЃљрЃЊрЃљрЃАрЃ«рЃЋрЃљ рЃцрЃЮрЃарЃЏрЃўрЃЌ:

