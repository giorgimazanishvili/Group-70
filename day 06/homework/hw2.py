# 2) рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃўрЃЌ рЃљрЃ«рЃАрЃћрЃюрЃўрЃЌ рЃарЃљ рЃљрЃарЃўрЃА sequences, iteration рЃЊрЃљ selection. рЃЏрЃЮрЃўрЃДрЃЋрЃљрЃюрЃћрЃЌ рЃЏрЃљрЃЌрЃў рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃћрЃЉрЃў.

# рЃЎрЃљрЃарЃњрЃљрЃЊ, рЃЦрЃЋрЃћрЃЏрЃЮрЃЌ рЃљрЃ«рЃАрЃюрЃўрЃџрЃўрЃљ **sequences**, **iteration** рЃЊрЃљ **selection**, рЃЏрЃљрЃЌ рЃерЃЮрЃарЃўрЃА рЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃўрЃљ рЃЌрЃўрЃЌрЃЮрЃћрЃБрЃџрЃў рЃфрЃюрЃћрЃЉрЃўрЃА рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃўрЃф. рЃћрЃА рЃбрЃћрЃарЃЏрЃўрЃюрЃћрЃЉрЃў рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃарЃћрЃЉрЃљрЃерЃў рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЋрЃљрЃюрЃўрЃљ рЃЊрЃљ рЃ«рЃерЃўрЃарЃљрЃЊ рЃњрЃЋрЃ«рЃЋрЃЊрЃћрЃЉрЃљ рЃюрЃћрЃЉрЃўрЃАрЃЏрЃўрЃћрЃарЃў рЃљрЃџрЃњрЃЮрЃарЃўрЃЌрЃЏрЃўрЃА рЃерЃћрЃЊрЃњрЃћрЃюрЃўрЃАрЃљрЃА.



# 1. **Sequences (рЃЏрЃўрЃЏрЃЊрЃћрЃЋрЃарЃЮрЃЉрЃљ)**

# **рЃарЃљрЃА рЃюрЃўрЃерЃюрЃљрЃЋрЃА?**
# Sequence рЃюрЃўрЃерЃюрЃљрЃЋрЃА рЃўрЃЏ рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃћрЃЉрЃўрЃА рЃџрЃЮрЃњрЃўрЃЎрЃБрЃа рЃЌрЃљрЃюрЃЏрЃўрЃЏрЃЊрЃћрЃЋрЃарЃЮрЃЉрЃљрЃА, рЃарЃЮрЃЏрЃџрЃћрЃЉрЃўрЃф рЃћрЃарЃЌрЃў рЃЏрЃћрЃЮрЃарЃўрЃА рЃерЃћрЃЏрЃЊрЃћрЃњ рЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ рЃќрЃћрЃЊрЃўрЃќрЃћрЃЊ, рЃДрЃЮрЃЋрЃћрЃџрЃњрЃЋрЃљрЃарЃў рЃњрЃљрЃЊрЃљрЃ«рЃЋрЃћрЃЋрЃўрЃА рЃњрЃљрЃарЃћрЃерЃћ.

# **рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў (Python-рЃерЃў):**

# ```python
# print("Step 1: Wake up")
# print("Step 2: Brush teeth")
# print("Step 3: Eat breakfast")
# ```

# ­Ъћ╣ **рЃњрЃљрЃюрЃЏрЃљрЃарЃбрЃћрЃЉрЃљ:** рЃћрЃА рЃЉрЃарЃФрЃљрЃюрЃћрЃЉрЃћрЃЉрЃў рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ рЃќрЃБрЃАрЃбрЃљрЃЊ рЃўрЃЏ рЃарЃўрЃњрЃўрЃЌ, рЃарЃЮрЃњрЃЮрЃарЃўрЃЌрЃљрЃф рЃгрЃћрЃарЃўрЃљ. рЃ»рЃћрЃа рЃњрЃљрЃЏрЃЮрЃўрЃбрЃљрЃюрЃћрЃЉрЃљ "Step 1", рЃерЃћрЃЏрЃЊрЃћрЃњ "Step 2" рЃЊрЃљ рЃЉрЃЮрЃџрЃЮрЃА "Step 3".

# ---

# 2. **Iteration (рЃўрЃбрЃћрЃарЃљрЃфрЃўрЃљ / рЃњрЃљрЃюрЃЏрЃћрЃЮрЃарЃћрЃЉрЃљ)**

# **рЃарЃљрЃА рЃюрЃўрЃерЃюрЃљрЃЋрЃА?**
# Iteration рЃљрЃарЃўрЃА рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃўрЃА рЃЏрЃарЃљрЃЋрЃљрЃџрЃ»рЃћрЃа рЃњрЃљрЃЏрЃћрЃЮрЃарЃћрЃЉрЃљ рЃЏрЃљрЃюрЃљрЃЏ, рЃАрЃљрЃюрЃљрЃЏ рЃњрЃљрЃарЃЎрЃЋрЃћрЃБрЃџрЃў рЃърЃўрЃарЃЮрЃЉрЃљ рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ рЃљрЃю рЃЊрЃљрЃЌрЃЦрЃЏрЃБрЃџрЃў рЃарЃљрЃЮрЃЊрЃћрЃюрЃЮрЃЉрЃљ рЃљрЃа рЃљрЃЏрЃЮрЃўрЃгрЃБрЃарЃћрЃЉрЃљ.

# **рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў (for рЃфрЃўрЃЎрЃџрЃў Python-рЃерЃў):**

# ```python
# for i in range(3):
#     print("Hello")
# ```

# ­Ъћ╣ **рЃњрЃљрЃюрЃЏрЃљрЃарЃбрЃћрЃЉрЃљ:** рЃћрЃА рЃЎрЃЮрЃЊрЃў рЃАрЃљрЃЏрЃ»рЃћрЃа рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА "Hello". `for` рЃфрЃўрЃЎрЃџрЃў рЃњрЃљрЃюрЃЏрЃћрЃЮрЃарЃћрЃЉрЃўрЃЌ рЃљрЃАрЃарЃБрЃџрЃћрЃЉрЃА рЃћрЃарЃЌрЃАрЃљ рЃЊрЃљ рЃўрЃЏрЃљрЃЋрЃћ рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃљрЃА.

# ---

# 3. **Selection (рЃљрЃарЃЕрЃћрЃЋрЃљ)**

# **рЃарЃљрЃА рЃюрЃўрЃерЃюрЃљрЃЋрЃА?**
# Selection рЃљрЃарЃўрЃА рЃњрЃљрЃЊрЃљрЃгрЃДрЃЋрЃћрЃбрЃўрЃџрЃћрЃЉрЃўрЃА рЃЏрЃўрЃдрЃћрЃЉрЃљ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљрЃерЃў Рђћ рЃарЃЮрЃЏрЃћрЃџрЃў рЃЉрЃџрЃЮрЃЎрЃў рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃА рЃЊрЃљрЃЏрЃЮрЃЎрЃўрЃЊрЃћрЃЉрЃБрЃџрЃўрЃљ рЃърЃўрЃарЃЮрЃЉрЃўрЃА рЃерЃћрЃАрЃарЃБрЃџрЃћрЃЉрЃљрЃќрЃћ (true/false).

# **рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў (if рЃърЃўрЃарЃЮрЃЉрЃўрЃЌрЃў рЃЮрЃърЃћрЃарЃљрЃбрЃЮрЃарЃў Python-рЃерЃў):**

# ```python
# age = 18
# if age >= 18:
#     print("You can vote")
# else:
#     print("You cannot vote")
# ```

# ­Ъћ╣ **рЃњрЃљрЃюрЃЏрЃљрЃарЃбрЃћрЃЉрЃљ:** рЃљрЃЦ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ рЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃърЃўрЃарЃЮрЃЉрЃљрЃА `age >= 18`. рЃЌрЃБ рЃърЃўрЃарЃЮрЃЉрЃљ рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ, рЃњрЃљрЃЏрЃЮрЃўрЃбрЃљрЃюрЃА "You can vote", рЃгрЃўрЃюрЃљрЃљрЃдрЃЏрЃЊрЃћрЃњ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў Рђћ "You cannot vote".

# ---

# рЃЌрЃБ рЃњрЃАрЃБрЃарЃА, рЃЊрЃљрЃЋрЃљрЃЏрЃљрЃбрЃЮ рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃћрЃЉрЃў рЃАрЃ«рЃЋрЃљ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃарЃћрЃЉрЃўрЃА рЃћрЃюрЃљрЃќрЃћ рЃљрЃю рЃЋрЃўрЃќрЃБрЃљрЃџрЃБрЃарЃў рЃЊрЃўрЃљрЃњрЃарЃљрЃЏрЃљ, рЃАрЃўрЃљрЃЏрЃЮрЃЋрЃюрЃћрЃЉрЃўрЃЌ рЃЊрЃљрЃњрЃћрЃ«рЃЏрЃљрЃарЃћрЃЉрЃў!
