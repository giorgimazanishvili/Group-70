# 4) კომენტარების სახით ახსენით Linear Gradient.

# Linear Gradient არის CSS თვისება, რომელიც ქმნის ფერის გადაზრდას (gradient) ორი ან მეტი ფერის
# შორის. ეს ეფექტი ხშირად გამოიყენება ფონების, ღილაკების და სხვა ელემენტების დიზაინში.
# Linear Gradient-ის შექმნისთვის გამოიყენება `linear-gradient` ფუნქცია, რომელიც იღებს
# ფერების ჩამონათვალს და მიმართულებას, რომელშიც უნდა მოხდეს გადაზრდა.