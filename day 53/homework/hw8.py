# 8) კომენტარების სახით ჩამოთვალეთ და ახსენით ყველა ასზომი ერთეული რაც ისწავლეთ.

# ასზომი ერთეულები (SI units) არის საერთაშორისო სისტემის მიერ განსაზღვრული ფუნდამენტური და წარმოებული ერთეულები, რომლებიც გამოიყენება ფიზიკურ რაოდენობათა გაზომვისთვის. აქ არის ძირითადი ასზომი ერთეულები:

# 1. მეტრი (m) - სიგრძის ერთეული
# 2. კილოგრამი (kg) - მასის ერთეული
# 3. წამი (s) - დროის ერთეული
# 4. ამპერი (A) - ელექტრო ნაკადის ერთეული
# 5. კელვინი (K) - ტემპერატურის ერთეული
# 6. მოლი (mol) - ნივთიერების რაოდენობის ერთეული
# 7. კანდელა (cd) - სინათლის ინტენსივობის ერთეული