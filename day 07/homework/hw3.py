# 3)კომენტარებით ახსენი while loop. 

 # ვიწყებთ ცვლადით i, რომელსაც ვანიჭებთ მნიშვნელობას 1
i = 1

# while ციკლი მუშაობს მანამ, სანამ პირობა (i <= 5) ჭეშმარიტია
while i <= 5:
    # ბეჭდავს i-ის მიმდინარე მნიშვნელობას
    print(i)
    
    # ზრდის i-ის მნიშვნელობას 1-ით, რათა გადავიდეს შემდეგ რიცხვზე
    i += 1

# როდესაც i გახდება 6, პირობა i <= 5 აღარ შესრულდება და ციკლი შეწყდება