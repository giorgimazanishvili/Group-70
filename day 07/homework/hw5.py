# 5)მომხმარებელმა უნდა შეიყვანოს რიცხვი n. პროგრამამ while ციკლით უნდა დაითვალოს 1-დან n-მდე რიცხვების ჯამი.

# ვთხოვთ მომხმარებელს რიცხვის შეყვანას
n = int(input("შეიყვანე მთელი რიცხვი n: "))

# საწყისი მნიშვნელობები
i = 1
sum = 0

# while ციკლი ითვლის 1-დან n-მდე რიცხვების ჯამს
while i <= n:
    sum += i  # sum = sum + i
    i += 1    # გადავდივართ შემდეგ რიცხვზე

# შედეგის გამოჩენა
print("რიცხვების ჯამი 1-დან", n, "-მდე არის:", sum)
