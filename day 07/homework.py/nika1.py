# 1)დაწერე პროგრამა, რომელიც while ციკლით დაბეჭდავს რიცხვებს 1-დან 10-მდე.

# ვაწესებთ საწყის მნიშვნელობას
number = 1

# ციკლი იმართება მანამ, სანამ number <= 10
while number <= 10:
    print(number)
    number += 1  # თითოეულ იტერაციაზე ვზრდით number-ს 1-ით

# 2)დაწერე პროგრამა, რომელიც დაბეჭდავს რიცხვებს 10-დან 1-მდე.
number = 1

while number <= 10:
    print(number, end=" ")  # რიცხვების ბეჭდა ერთ ხაზზე, სივრცით გამოყოფილი
    number += 1

# 3)კომენტარებით ახსენი while loop. 

# while ციკლი არის კონტროლური სტრუქტურა, რომელიც 반복ებით ასრულებს კოდს,
# სანამ მოცემული პირობა (condition) არის True.

number = 1  # ვაწესებთ საწყის მნიშვნელობას

while number <= 10:  # ეს არის ციკლის თავი, სადაც ხდება პირობის შემოწმება
    print(number)     # ეს არის ციკლის სხეული, რომელიც 실행დება ყოველ ჯერზე, როცა პირობა Trueა
    number += 1       # ამით ვზრდით number-ს, რომ ციკლმა ერთხელ მაინც შეწყვიტოს მუშაობა

# როცა პირობა number <= 10 აღარ იკმაყოფილება (number გახდება 11), ციკლი დასრულდება.

# 4)დაწერე პროგრამა, რომელიც სთხოვს მომხმარებელს პაროლის შეყვანას. სწორი პაროლია "python123". სანამ სწორად არ შეიყვანს, მოთხოვე თავიდან.

# სწორი პაროლი
correct_password = "python123"

# ვიწყებთ უსასრულო ციკლს
while True:
    password = input("შეიყვანე პაროლი: ")
    
    # თუ პაროლი სწორია, გამოვალთ ციკლიდან და ვბეჭდავთ მისასალმებელს
    if password == correct_password:
        print("პაროლი სწორია! შეხვედით სისტემაში.")
        break  # ციკლის შეწყვეტა
    
    # თუ პაროლი არასწორია, კვლავ მოვთხოვთ შეყვანას
    else:
        print("პაროლი არასწორია. სცადე ისევ.")

# 5)მომხმარებელმა უნდა შეიყვანოს რიცხვი n. პროგრამამ while ციკლით უნდა დაითვალოს 1-დან n-მდე რიცხვების ჯამი.
 
# მომხმარებლიდან წაიკითხეთ რიცხვი n
n = int(input("შეიყვანე რიცხვი n: "))

# საწყისი მნიშვნელობები
sum_numbers = 0  # ჯამი
i = 1            # მიმდევრობის დაწყება 1-დან

# ციკლი მუშაობს მანამ, სანამ i <= n
while i <= n:
    sum_numbers += i  # ჯამში ვუმატებთ i-ს
    i += 1            # ვზრდით i-ს 1-ით

# საბოლოო შედეგის დაბეჭდვა
print("ჯამი 1-დან", n, "მდე არის:", sum_numbers)
