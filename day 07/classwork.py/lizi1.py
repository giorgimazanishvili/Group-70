while True:
    print("ეს ციკლი უსასრულოდ მუშაობს")

# 2) გამოიტანეთ მომხმარებლის შემოყვანილი სახელი 10-ჯერ იტერაციის მეშვეობით.

# მომხმარებელს ვთხოვთ სახელს
name = input("შეიყვანე შენი სახელი: ")

# 10-ჯერ ვბეჭდავთ სახელს
for i in range(10):
    print(name)

# 3) შექმენით while loop და კომენტარად მიაწერეთ რომელია თავი და რომელი სხეული.

# while loop-ის თავი (header) — პირობა, რომლის მიხედვითაც ციკლი იმართება
while True:  
    # while loop-ის სხეული (body) — კოდი, რომელიც ციკლში შესრულდება
    print("ეს არის loop-ის სხეული")
    break  # ციკლის შეწყვეტის მაგალითი, რომ არ იყოს უსასრულო

# 4) მომხმარებელს შემოაყვანინეთ რიცხვი, სანამ ეს რიცხვი არ იქნება 100-ზე მეტი მაქამდე თავიდან მოთხოვთ რიცხვის შემოტანა.
# მომხმარებელს ვთხოვთ რიცხვის შეყვანას
number = int(input("შეიყვანე რიცხვი: "))

# ციკლი იმართება მანამ, სანამ რიცხვი არ იქნება 100-ზე მეტი
while number <= 100:
    print("რიცხვი პატარაა ან ტოლი 100-ისა, ისევ სცადე.")
    number = int(input("შეიყვანე რიცხვი: "))

print("რიცხვი უკვე 100-ზე მეტი:", number)

# 5) მომხმარებელს შემოატანინეთ სხვადასხვა სახელები 5-ჯერ იტერაციის მეშვეობით, შექმენით ამ სახელებისთვის ცვლადი რომელშიც ყოველი სახელი დაემატება და ეს სახელები გამოიყოფა მძიმეებით, ანუ თქვენი ცვლადი 5 სახელის შეყვანის შემდეგ უნდაგამოიყურებოდეს ასე: "{სახელი პირველი}, {სახელი მეორე}, ... {სახელი მეხუთე}". 

# ცვლადი, რომელშიც შევინახავთ ყველა სახელს
names = ""

# 5-ჯერ მოვთხოვთ მომხმარებელს სახელს და შევინახავთ ცვლადში
for i in range(5):
    name = input(f"შეიყვანე სახელი {i + 1}: ")
    
    # თუ ეს პირველი სახელი არაა, უნდა დავამატოთ მძიმი წინა სახელებს შორის
    if i == 0:
        names = name
    else:
        names += ", " + name

# შედეგის დაბეჭდვა
print("შეყვანილი სახელები:", names)
