# 3) შექმენით while loop და კომენტარად მიაწერეთ რომელია თავი და რომელი სხეული.

# თავი: ციკლის დაწყების პირობა
while True:  # თავი (loop condition)

    # სხეული: შესრულების კოდი, რომელიც ციკლში იწერება
    print("ეს არის ციკლის სხეული")

    # გამოსვლის პირობის შემოწმება, რომ არ იყოს უსასრულო
    break  # ამ შემთხვევაში ციკლი ერთი ნახვით დასრულდება