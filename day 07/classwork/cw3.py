# 3) შექმენით while loop და კომენტარად მიაწერეთ რომელია თავი და რომელი სხეული.

# while-ის თავი (head)
while True:
    # while-ის სხეული (body)
    print("ეს ციკლი უსასრულოდ იმუშავებს")
    break  # ამით ვწყვეტთ ციკლს, რომ უსასრულოდ არ იმუშავოს.

# ახსნა:

# . while-ის თავი: while True: -- ეს არის პირობა, რომლის მიხედვითაც ციკლი იმუშავებს.
# . while-ის სხეული: {print(...); break} -- ეს არის ბლოკი, რომელიც შესრულდება, სანამ პირობა მართალია.


