# 1) რაში გვეხმარება Indexing-ი?
# შესანიშნავია! აი **სამი უფრო მარტივი დავალება**, რაც ინდექსების გამოყენებას ეხება და შესაფერისია დამწყებთათვის:

# Indexing (ინდექსირება) ძალიან სასარგებლოა მონაცემებთან სწრაფი წვდომისთვის, განსაკუთრებით მონაცემთა სტრუქტურებში, როგორიცაა სიები (lists), სტრიქონები (strings), მასივები (arrays) და მონაცემთა ბაზები (databases). ინდექსირების დახმარებით შეგვიძლია:

# მონაცემებზე წვდომა კონკრეტული მდებარეობიდან – ინდექსით შეგვიძლია მივწვდეთ კონკრეტულ ელემენტს სიის, სტრიქონის ან მასივის შიგნით.

# მონაცემების შეცვლა ან წაშლა ინდექსით – შეგვიძლია კონკრეტული ინდექსით ელემენტი შევცვალოთ ან ამოვშალოთ.

# მონაცემების ნაწილობრივი წაკითხვა (slicing) – ინდექსების გამოყენებით შეგვიძლია ამოვჭრათ სიის ან სტრიქონის ნაწილი.