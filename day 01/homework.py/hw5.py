# 6) კომენტარებით ახსენით რა განსხვავებაა თითოეულ მონაცემთა ტიპში.
# Integer (int) - მთელი რიცხვები, რომლებიც არ შეიცავს ათწილადი ნაწილს.
# მაგალითად: 1, -5, 100
my_age = 25   # 25 არის მთელი რიცხვი (Integer)

# Float - ლურსმული რიცხვები, რომლებიც შეიცავს ათწილადი ნაწილს.
# მაგალითად: 3.14, -5.7, 0.99
height = 1.75  # 1.75 არის ლურსმული რიცხვი (Float)

# String (str) - ტექსტური მონაცემები, რომლებიც ციტატებში ინახება.
# მაგალითად: "Hello", "Python", "123"
name = "Alice"  # "Alice" არის ტექსტური მონაცემი (String)

# Boolean (bool) - ლოგიკური ტიპი, რომელიც შეიძლება მიიღოს მხოლოდ ორი მნიშვნელობა: True ან False.
# გამოიყენება ლოგიკურ ოპერაციებში.
# მაგალითად: True, False
has_pet = True  # True ნიშნავს, რომ لديك შინაური ცხოველი

# List - მონაცემთა მილი, რომელიც შეიცავს მრავალ ელემენტს და შეიძლება შეიცვალოს.
# გამოიყენება თუ გვსურს შევინახოთ ერთი ტიპის ან სხვადასხვა ტიპის ელემენტები ერთ ლისტში.
# მაგალითად: [1, 2, 3], ["apple", "banana"], [True, False, True]
fruits = ["apple", "banana", "cherry"]  # ამ ლისტში ელემენტები არიან ხილი

