# 4) შექმენით 5 ცვლადი სადაც შეინახავთ რიცხვებს, მაგალითად: (15, 432, 25, 3, 45, 346) ამ ცვლადებზე უნდა მოახდინოთ 
# რიცხვები ცვლადებში
num1 = 15
num2 = 432
num3 = 25
num4 = 3
num5 = 45

# ზოგიერთი ოპერაციის მაგალითი

# ამავე დროს დავამატოთ, გამოვაკლოთ, გავამრავლოთ და დავყოთ
sum_result = num1 + num2 + num3 + num4 + num5  # ჯამი
difference_result = num2 - num1  # სხვაობა
product_result = num1 * num3  # გამრავლება
division_result = num5 / num4  # გამყოფა (დამთხვევა)

# შედეგების გამოტანა
print(f"Sum: {sum_result}")
print(f"Difference: {difference_result}")
print(f"Product: {product_result}")
print(f"Division: {division_result:.2f}")  # ორი ნიშანი შემდეგი კომა

