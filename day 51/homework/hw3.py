# 3) ახსენი რა განსხვავებაა @keyframes-სა და transform-transition-ით შექმნილ ანიმაციას შეორის.

# @keyframes და transform-transition ორივე CSS ტექნიკაა ანიმაციების შესაქმნელად, მაგრამ მათ შორის არსებობს მნიშვნელოვანი განსხვავებები:

# 1. @keyframes საშუალებას გაძლევთ შექმნათ კომპლექსური ანიმაციები, რომლებიც შეიცავს რამდენიმე ნაბიჯს და მდგომარეობას, ხოლო transform-transition ძირითადად გამოიყენება მარტივი ტრანზიციებისათვის, როგორიცაა ელემენტის ზომის ან პოზიციის ცვლილება.

# 2. @keyframes-ის გამოყენებით შეგიძლიათ განსაზღვროთ ანიმაციის ხანგრძლივობა, დროის ფუნქცია და სხვა პარამეტრები, რაც უფრო დიდი კონტროლი გაწვდით ანიმაციის პროცესზე. transform-transition უფრო შეზღუდულია და ძირითადად ორიენტირებულია ელემენტის მდგომარეობის ცვლილებაზე.

# 3. @keyframes-ის გამოყენება მოითხოვს უფრო მეტი კოდის დაწერას და კომპლექსურობას, ხოლო transform-transition უფრო მარტივი და სწრაფი გამოსაყენებელია.