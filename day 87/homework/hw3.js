// 3) გაქვს ორი ობიექტი:
// const obj1 = { a: 1, b: 2 };
// const obj2 = { b: 20, c: 3 };
// შექმენი ახალი ობიექტი მათი გაერთიანებით.
// დააკვირდი — რომელი b დარჩება საბოლოო ობიექტში?

const obj1 = { a: 1, b: 2 };
const obj2 = { b: 20, c: 3 };

// ობიექტების გაერთიანება spread-ის გამოყენებით
const merged = { ...obj1, ...obj2 };

console.log(merged);
/*
გამოსახვა:
{
  a: 1,
  b: 20, // obj2–ის b გადაფარავს obj1–ის b
  c: 3
}
*/