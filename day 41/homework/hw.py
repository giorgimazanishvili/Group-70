# 1) კომენტარების სახით ახსენით static, relative, absolute.

# static - ეს არის ფაილის ან რესურსის სრული გზა, რომელიც არ იცვლება და ყოველთვის იგივე რჩება.
# relative - ეს არის ფაილის ან რესურსის გზა, რომელიც დამოკიდებულია მიმდინარე სამუშაო დირექტორიაზე და შეიძლება შეიცვალოს.
# absolute - ეს არის ფაილის ან რესურსის სრული გზა, რომელიც იწყება ფაილური სისტემის root-დან და ყოველთვის იგივე რჩება.