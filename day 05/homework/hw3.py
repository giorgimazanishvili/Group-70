# 3)рЃљрЃ«рЃАрЃћрЃюрЃўрЃЌ рЃЊрЃдрЃћрЃА рЃюрЃљрЃАрЃгрЃљрЃЋрЃџрЃў If рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃўрЃЌ.

# рЃарЃЮрЃњрЃЮрЃарЃф рЃгрЃћрЃАрЃў, **`if`**-рЃўрЃА рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃљ Python-рЃерЃў рЃАрЃљрЃерЃБрЃљрЃџрЃћрЃЉрЃљрЃА рЃњрЃЋрЃљрЃФрЃџрЃћрЃЋрЃА рЃерЃћрЃЋрЃљрЃЏрЃЮрЃгрЃЏрЃЮрЃЌ рЃърЃўрЃарЃЮрЃЉрЃљ рЃЊрЃљ, рЃЌрЃБ рЃўрЃА рЃЏрЃљрЃарЃЌрЃљрЃџрЃўрЃљ, рЃерЃћрЃЋрЃљрЃАрЃарЃБрЃџрЃЮрЃЌ рЃњрЃљрЃарЃЎрЃЋрЃћрЃБрЃџрЃў рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃћрЃЉрЃў. рЃљрЃЦ рЃљрЃЏ рЃАрЃљрЃЎрЃўрЃЌрЃ«рЃЌрЃљрЃю рЃЊрЃљрЃЎрЃљрЃЋрЃерЃўрЃарЃћрЃЉрЃўрЃЌ рЃарЃљрЃЏрЃЊрЃћрЃюрЃўрЃЏрЃћ рЃФрЃўрЃарЃўрЃЌрЃљрЃЊрЃў рЃърЃарЃўрЃюрЃфрЃўрЃърЃўрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃАрЃљрЃф рЃЊрЃдрЃћрЃА рЃњрЃљрЃюрЃЋрЃўрЃ«рЃўрЃџрЃљрЃЋрЃЌ:

# 1. **if-рЃўрЃА рЃАрЃўрЃюрЃбрЃљрЃЦрЃАрЃў**:

# ```python
# if condition:
#     # рЃћрЃА рЃЎрЃЮрЃЊрЃў рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃўрЃЏ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў, рЃарЃЮрЃЊрЃћрЃАрЃљрЃф condition рЃўрЃЦрЃюрЃћрЃЉрЃљ True
# ```

# * **`if`** рЃљрЃарЃўрЃА рЃЎрЃЮрЃюрЃЊрЃўрЃфрЃўрЃБрЃарЃў рЃњрЃљрЃЏрЃЮрЃ«рЃљрЃбрЃБрЃџрЃћрЃЉрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃерЃћрЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃЌрЃБ рЃарЃљ рЃърЃўрЃарЃЮрЃЉрЃћрЃЉрЃў рЃўрЃФрЃћрЃюрЃА рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃљрЃА.
# * рЃЌрЃБ **condition** (рЃърЃўрЃарЃЮрЃЉрЃљ) рЃЏрЃљрЃарЃЌрЃљрЃџрЃўрЃљ, рЃЏрЃљрЃерЃўрЃю рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ рЃЎрЃЮрЃЊрЃў, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃЏрЃљрЃА рЃерЃћрЃЏрЃЊрЃћрЃњ рЃўрЃЦрЃюрЃћрЃЉрЃљ рЃЏрЃўрЃЌрЃўрЃЌрЃћрЃЉрЃБрЃџрЃў.

# ### 2. **`if`-рЃўрЃА рЃАрЃбрЃарЃБрЃЦрЃбрЃБрЃарЃљ**:

# ```python
# if condition:
#     # рЃЎрЃЮрЃЊрЃў, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ, рЃЌрЃБ condition рЃљрЃарЃўрЃА True
# elif other_condition:
#     # рЃЎрЃЮрЃЊрЃў, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ, рЃЌрЃБ first condition рЃљрЃа рЃњрЃљрЃЏрЃљрЃарЃЌрЃџрЃЊрЃћрЃЉрЃљ,
#     # рЃЏрЃљрЃњрЃарЃљрЃЏ second condition (other_condition) рЃњрЃљрЃЏрЃљрЃарЃЌрЃџрЃЊрЃћрЃЉрЃљ
# else:
#     # рЃЎрЃЮрЃЊрЃў, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ, рЃЌрЃБ рЃљрЃарЃф рЃћрЃарЃЌрЃў condition рЃљрЃа рЃњрЃљрЃЏрЃљрЃарЃЌрЃџрЃЊрЃљ
# ```

# * **`elif`** (else if) рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ, рЃарЃЮрЃЊрЃћрЃАрЃљрЃф рЃБрЃюрЃЊрЃљ рЃерЃћрЃљрЃЏрЃЮрЃгрЃЏрЃЮрЃЌ рЃарЃљрЃЏрЃЊрЃћрЃюрЃўрЃЏрЃћ рЃърЃўрЃарЃЮрЃЉрЃљ рЃћрЃарЃЌрЃЊрЃарЃЮрЃБрЃџрЃљрЃЊ.
# * **`else`** рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃАрЃ«рЃЋрЃљ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃћрЃЉрЃўрЃАрЃЌрЃЋрЃўрЃА, рЃарЃЮрЃЊрЃћрЃАрЃљрЃф рЃљрЃарЃф рЃћрЃарЃЌрЃў рЃърЃўрЃарЃЮрЃЉрЃљ рЃљрЃа рЃњрЃљрЃЏрЃљрЃарЃЌрЃџрЃЊрЃћрЃЉрЃљ.

# ### 3. **`if`-рЃўрЃА рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў**:

# ```python
# num = 15
# if num > 10:
#     print("This number is greater than 10")
# else:
#     print("This number is 10 or smaller")
# ```

# * рЃЌрЃБ **`num > 10`**, рЃњрЃљрЃЏрЃЮрЃЋрЃљ `"This number is greater than 10"`.
# * рЃЌрЃБ рЃљрЃа рЃљрЃарЃўрЃА, рЃЏрЃљрЃерЃўрЃю рЃњрЃљрЃЏрЃЮрЃЊрЃўрЃА `"This number is 10 or smaller"`.

# ### 4. **рЃЏрЃарЃљрЃЋрЃљрЃџрЃцрЃћрЃарЃЮрЃЋрЃљрЃюрЃў рЃерЃћрЃЊрЃљрЃарЃћрЃЉрЃћрЃЉрЃў**:

# рЃЕрЃЋрЃћрЃю рЃерЃћрЃњрЃЋрЃўрЃФрЃџрЃўрЃљ рЃњрЃљрЃЏрЃЮрЃЋрЃўрЃДрЃћрЃюрЃЮрЃЌ рЃљрЃарЃљрЃЏрЃљрЃарЃбрЃЮ `if`-рЃўрЃА рЃЊрЃљрЃАрЃљрЃгрЃДрЃўрЃАрЃерЃў рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃћрЃарЃЌрЃў рЃърЃўрЃарЃЮрЃЉрЃљ, рЃљрЃарЃљрЃЏрЃћрЃЊ рЃЊрЃљрЃЋрЃљрЃДрЃћрЃюрЃЮрЃЌ рЃБрЃцрЃарЃЮ рЃЎрЃЮрЃЏрЃърЃџрЃћрЃЦрЃАрЃБрЃарЃў рЃњрЃљрЃЏрЃЮрЃюрЃљрЃЎрЃџрЃўрЃАрЃћрЃЉрЃў:

# ```python
# num1 = 5
# num2 = 10

# # рЃерЃћрЃњрЃЋрЃўрЃФрЃџрЃўрЃљ рЃерЃћрЃЋрЃљрЃЏрЃЮрЃгрЃЏрЃЮрЃЌ рЃЊрЃљ рЃАрЃ«рЃЋрЃљрЃЊрЃљрЃАрЃ«рЃЋрЃљ рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃћрЃЉрЃў рЃЋрЃљрЃЎрЃћрЃЌрЃЮрЃЌ
# if num1 < num2:
#     print("num1 is less than num2")
# ```

# ### 5. **`if`-рЃўрЃА рЃњрЃљрЃарЃћрЃерЃћ рЃљрЃџрЃбрЃћрЃарЃюрЃљрЃбрЃўрЃЋрЃћрЃЉрЃў**:

# рЃарЃЮрЃњрЃЮрЃарЃф рЃЕрЃЋрЃћрЃю рЃЊрЃљрЃЋрЃўрЃюрЃљрЃ«рЃћрЃЌ, `if`-рЃўрЃА рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃљ рЃерЃћрЃАрЃљрЃФрЃџрЃћрЃЉрЃћрЃџрЃўрЃљ рЃАрЃ«рЃЋрЃљрЃЊрЃљрЃАрЃ«рЃЋрЃљ рЃцрЃЮрЃарЃЏрЃўрЃЌ. рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃљрЃЊ, ternary operator-рЃўрЃА рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃўрЃЌ:

# ```python
# # ternary operator
# print("You are right" if num1 > num2 else "You are wrong")
# ```

# рЃљрЃЏ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў, рЃћрЃА рЃћрЃарЃЌрЃў рЃ«рЃљрЃќрЃўрЃљ, рЃАрЃљрЃЊрЃљрЃф рЃърЃўрЃарЃЮрЃЉрЃљ рЃерЃћрЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃљ рЃЊрЃљ рЃЮрЃарЃў рЃерЃћрЃАрЃљрЃФрЃџрЃЮ рЃерЃћрЃЊрЃћрЃњрЃў рЃўрЃЦрЃюрЃћрЃЉрЃљ.

# #  6. **рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃў**:

# рЃДрЃЋрЃћрЃџрЃљ Python рЃЎрЃЮрЃЊрЃерЃў рЃерЃћрЃњрЃўрЃФрЃџрЃўрЃљрЃЌ рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃЮрЃЌ **рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃў** (рЃарЃЮрЃЏрЃџрЃћрЃЉрЃўрЃф рЃўрЃгрЃДрЃћрЃЉрЃљ `#`-рЃўрЃЌ) рЃўрЃЏрЃўрЃАрЃљрЃЌрЃЋрЃўрЃА, рЃарЃЮрЃЏ рЃњрЃљрЃљрЃарЃЎрЃЋрЃўрЃЮрЃЌ, рЃарЃљрЃА рЃљрЃЎрЃћрЃЌрЃћрЃЉрЃА рЃЌрЃўрЃЌрЃЮрЃћрЃБрЃџрЃў рЃЎрЃЮрЃЊрЃў. рЃћрЃА рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃЎрЃЮрЃЊрЃќрЃћ рЃўрЃюрЃцрЃЮрЃарЃЏрЃљрЃфрЃўрЃўрЃА рЃЏрЃўрЃфрЃћрЃЏрЃљрЃА рЃћрЃЏрЃАрЃљрЃ«рЃБрЃарЃћрЃЉрЃљ рЃЊрЃљ рЃљрЃа рЃњрЃљрЃЋрЃџрЃћрЃюрЃљрЃА рЃљрЃ«рЃЊрЃћрЃюрЃА рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃА рЃерЃћрЃАрЃарЃБрЃџрЃћрЃЉрЃљрЃќрЃћ.

# **рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў**:

# ```python
# # рЃћрЃА рЃљрЃарЃўрЃА if рЃАрЃбрЃарЃБрЃЦрЃбрЃБрЃарЃўрЃА рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў
# if num > 10:  # рЃЌрЃБ рЃарЃўрЃфрЃ«рЃЋрЃў 10-рЃќрЃћ рЃЏрЃћрЃбрЃўрЃљ
#     print("Number is greater than 10")  # рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА рЃЌрЃБ рЃЏрЃљрЃарЃЌрЃљрЃџрЃўрЃљ
# else:
#     print("Number is 10 or smaller")  # рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА рЃЌрЃБ рЃљрЃа рЃљрЃарЃўрЃА 10-рЃќрЃћ рЃЏрЃћрЃбрЃў
# ```

# **рЃњрЃљрЃЊрЃљрЃАрЃљрЃгрЃДрЃЋрЃћрЃбрЃў рЃЎрЃўрЃЌрЃ«рЃЋрЃћрЃЉрЃў**:

# * рЃЌрЃБ рЃърЃўрЃарЃЮрЃЉрЃљ рЃњрЃљрЃЏрЃљрЃарЃЌрЃџрЃЊрЃљ, рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ рЃЎрЃЮрЃЊрЃў, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃњрЃљрЃюрЃўрЃАрЃљрЃќрЃдрЃЋрЃарЃљ `if` рЃЉрЃџрЃЮрЃЎрЃерЃў.
# * рЃАрЃ«рЃЋрЃљ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљрЃерЃў, рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ `else` рЃљрЃю `elif` рЃЊрЃљ рЃЏрЃљрЃЌрЃў рЃерЃћрЃАрЃљрЃЉрЃљрЃЏрЃўрЃАрЃў рЃЎрЃЮрЃЊрЃћрЃЉрЃў.

# **рЃЊрЃљрЃАрЃЎрЃЋрЃюрЃљ**: `if`-рЃўрЃА рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃљ рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЋрЃљрЃюрЃўрЃљ, рЃарЃЮрЃЊрЃћрЃАрЃљрЃф рЃАрЃљрЃГрЃўрЃарЃЮрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃћрЃарЃЌрЃЮрЃЉрЃџрЃўрЃЋрЃў рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃўрЃА рЃерЃћрЃАрЃарЃБрЃџрЃћрЃЉрЃљ рЃњрЃљрЃарЃЎрЃЋрЃћрЃБрЃџрЃў рЃърЃўрЃарЃЮрЃЉрЃўрЃА рЃЏрЃўрЃ«рЃћрЃЊрЃЋрЃўрЃЌ. Python-рЃерЃў рЃърЃўрЃарЃЮрЃЉрЃљ рЃЏрЃљрЃарЃбрЃўрЃЋрЃљрЃЊ рЃЊрЃљ рЃњрЃљрЃАрЃљрЃњрЃћрЃЉрЃљрЃЊ рЃњрЃљрЃюрЃўрЃАрЃљрЃќрЃдрЃЋрЃарЃћрЃЉрЃљ.

# рЃЌрЃБ рЃарЃљрЃўрЃЏрЃћ рЃЊрЃљрЃЏрЃљрЃ«рЃљрЃАрЃўрЃљрЃЌрЃћрЃЉрЃћрЃџ рЃЎрЃўрЃЌрЃ«рЃЋрЃћрЃЉрЃА рЃњрЃЎрЃўрЃЌрЃ«рЃљрЃЋрЃЌ, рЃЌрЃБрЃюрЃЊрЃљрЃф рЃЎрЃЮрЃюрЃЎрЃарЃћрЃбрЃБрЃџ рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃћрЃЉрЃќрЃћ, рЃЏрЃўрЃЌрЃ«рЃљрЃарЃўрЃЌ! ­Ъўі