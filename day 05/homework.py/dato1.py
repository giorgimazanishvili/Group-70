# 2)მომხმარებელს შემოატანინეთ ორი რიცხვი გააკეთეთ მათზე მათემატიკური ოპერაციები  ("+, -, *, /, %, <, >, <=, >=, ==, !=. **) 
# შემოატანინეთ ორი რიცხვი მომხმარებლისგან
num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))

# მათემატიკური ოპერაციები
print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} * {num2} = {num1 * num2}")
print(f"{num1} / {num2} = {num1 / num2 if num2 != 0 else 'არ შეიძლება გან Teilen 0-თან'}")
print(f"{num1} % {num2} = {num1 % num2}")
print(f"{num1} ** {num2} = {num1 ** num2}")

# შედარებები
print(f"{num1} < {num2} = {num1 < num2}")
print(f"{num1} > {num2} = {num1 > num2}")
print(f"{num1} <= {num2} = {num1 <= num2}")
print(f"{num1} >= {num2} = {num1 >= num2}")
print(f"{num1} == {num2} = {num1 == num2}")
print(f"{num1} != {num2} = {num1 != num2}")

