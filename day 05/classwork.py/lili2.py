# 2) შემოატანინეთ მომხმარებელს ორი სტრინგი და გამოიტანეთ მათ გაერთიანება 

# შემოატანინეთ ორი სტრინგი მომხმარებლიდან
str1 = input("შეიყვანეთ პირველი სტრინგი: ")
str2 = input("შეიყვანეთ მეორე სტრინგი: ")

# სტრინგების გაერთიანება
result = str1 + str2

# შედეგის გამოჩენა
print(f"სტრინგების გაერთიანება: {result}")
