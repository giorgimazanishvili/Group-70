// 3) შექმენი მასივი სახელებით და forEach გამოიყენე ისე, რომ თითოეული სახელი დაბეჭდო ტექსტით: „გამარჯობა, [სახელი]“.

// მასივი სახელებით
const names = ["ნინო", "თათია", "გიორგი", "ლევანი"];

// თითოეული სახელი კონსოლში დაბეჭდვა ტექსტით "გამარჯობა, [სახელი]"
names.forEach(function(name) {
  console.log("გამარჯობა, " + name);
});

names.forEach(name => console.log(`გამარჯობა, ${name}`));