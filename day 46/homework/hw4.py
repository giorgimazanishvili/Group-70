# 4) ახსენით scroll, და გააკეთეთ 2 მაგალითი

# Scroll არის ფუნქცია, რომელიც გამოიყენება მონაცემთა სტრუქტურებში, როგორიცაა სიები, ტუპლები, ან სხვა კოლექციები, რათა გადაადგილდეს ან "გადახტეს" ელემენტებს შორის. ეს ფუნქცია ხშირად გამოიყენება გრაფიკულ ინტერფეისებში (GUI) ან ვებგვერდებზე, სადაც მომხმარებლებს შეუძლიათ გადაადგილდნენ დიდი რაოდენობის ინფორმაციის საშუალებით.

# მაგალითი 1: ვებსაიტზე სქროლვა
# როდესაც მომხმარებელი ვებსაიტზე სქროლავს, ის გადაადგილდება გვერდზე არსებული სხვადასხვა ელემენტების, როგორიცაა ტექსტი, სურათები და ბმულები, შორის.

# მაგალითი 2: სია ელემენტების სქროლვა
# როდესაც მომხმარებელი სიაში ელემენტებს შორის სქროლავს, ის ხედავს სიაში არსებული სხვადასხვა ელემენტების, როგორიცაა პროდუქტის სახელი, ფასი და აღწერა, შორის.