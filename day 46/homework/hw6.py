# 6) ახსენით overflow-x და overflow-y და გააკეთეთ 2 მაგალითი

# overflow-x და overflow-y არის CSS თვისებები, რომლებიც განსაზღვრავენ, თუ როგორ უნდა მოიქცეს ელემენტი, როდესაც მისი შინაარსი გადადის მისი განსაზღვრული განზომილებების ფარგლებს გარეთ.

# overflow-x - მართავს ჰორიზონტალურ გადახვედრას
# overflow-y - მართავს ვერტიკალურ გადახვედრას

# მაგალითი 1: overflow-x
# <div style="width: 200px; overflow-x: auto;">
#   <p>ეს არის ძალიან გრძელი ტექსტი, რომელიც არ ეტევა განსაზღვრულ ფართობში.</p>
# </div>

# მაგალითი 2: overflow-y
# <div style="height: 100px; overflow-y: scroll;">
#   <p>ეს არის ძალიან მაღალი ტექსტი, რომელიც არ ეტევა განსაზღვრულ სიმაღლეში.</p>
# </div>