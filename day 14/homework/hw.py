# 1) რისთვის ვიყენებთ Html-ს? დაწერეთ კომნტარებით.

# html - ლს ვიყენებთ საიტების შექნაში  

# HTML-ში ბევრი სხვადასხვა თაგი (tags) არსებობს, მაგრამ აქ არის რამდენიმე ძირითადი, ყველაზე ხშირად გამოყენებული:

# 1. <html> — მთელი HTML დოკუმენტის დასაწყისი.
# 2. <head> — მჟღავნდება დოკუმენტის მეტა-ინფორმაცია (მაგ: ქუდი, გამეორება, სტილები, სკრიპტები).
# 3. <body> — ყველა მთავარი კონტენტი (ტექსტი, გამოსახულებები და ა.შ.).
# 4. <title>— დოკუმენტის სათაური, რომელიც გამოჩნდება ბრაუზერის ტაბში.
# 5. <h1> - <h6> — სათაურები, <h1> ყველაზე მაღალი დონის სათაურია.
# 6. <p> — პარაგრაფი (ტექსტის ბლოკი).
# 7. <a> — ჰიპერლინკი (ბმული).
# 8. <img> — გამოსახულება.
# 9. <ul> — უჯრედების ჩამონათვალი (unordered list).
# 10. <ol> — რიცხვითი ჩამონათვალი (ordered list).
# 11. <li> — ჩამონათვალის ელემენტი (list item).
# 12. <table> — ცხრილი.
# 13. <tr> — ცხრილის რიგი (table row).
# 14. <td> — ცხრილის სიგრძე (table data).
# 15. <th> — ცხრილის სიგრძე, რომელიც შევსებულია სათაურით (table header).
# 16. <div> — განყოფილება ან კონტეინერი (block-level element).
# 17. <span> — inline ელემენტი, რომელსაც შეუძლია სტილიზაცია.
# 18. <form> — ფორმა.
# 19. <input> — ფორმის ველი (input field).
# 20. <button> — ღილაკი.
# 21. <link> — რესურსის დაკავშირება (მაგ: სტილის ფაილი).
# 22. <meta> — მეტა-ინფორმაცია დოკუმენტისთვის (მაგ: შიფრირება, რობოტები).
# 23. <style> — სტილის დამატება დოკუმენტში.
# 24. <script> — JavaScript-ის სკრიპტი.
# 25. <footer> — ფუტერი (დოკუმენტის ქვედა ნაწილი).
# 26. <header> — ჰედერი (დოკუმენტის ზედა ნაწილი).
# 27. <article> — დამოუკიდებელი ნაწილი დოკუმენტში.
# 28. <section> — სექცია, დოკუმენტში განყოფილება.
# 29. <aside> — გვერდითი კონტენტი, რომელიც შეიძლება იყოს ცალკე კონტექსტში.

# ეს მხოლოდ რამდენიმე ძირითად თაგს მოიცავს. HTML-ის ბევრი სხვა თაგი არსებობს, რომლებიც სპეციფიკურ მიზნებს ემსახურება.
