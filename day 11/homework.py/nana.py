has_card_input = input("рЃњрЃљрЃЦрЃЋрЃА рЃАрЃбрЃБрЃЊрЃћрЃюрЃбрЃБрЃарЃў рЃЉрЃљрЃарЃљрЃЌрЃў? (рЃЊрЃўрЃљрЃ«/рЃљрЃарЃљ): ").strip().lower()

