# 3) рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃАрЃЌрЃ«рЃЮрЃЋрЃћ рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃЮрЃА рЃЮрЃарЃў рЃарЃўрЃфрЃ«рЃЋрЃў. рЃерЃћрЃЏрЃЊрЃћрЃњ:
# рЃЌрЃБ рЃЮрЃарЃўрЃЋрЃћ рЃарЃўрЃфрЃ«рЃЋрЃў рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃўрЃљ Рєњ рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃћ "рЃЮрЃарЃўрЃЋрЃћ рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃўрЃљ"
# рЃЌрЃБ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃћрЃарЃЌрЃў рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃўрЃљ Рєњ "рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃћрЃарЃЌрЃў рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃў рЃарЃўрЃфрЃ«рЃЋрЃўрЃљ"
# рЃЌрЃБ рЃљрЃарЃфрЃћрЃарЃЌрЃў рЃљрЃа рЃљрЃарЃўрЃА рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃў Рєњ "рЃљрЃарЃфрЃћрЃарЃЌрЃў рЃљрЃа рЃљрЃарЃўрЃА рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃў"

# рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃњрЃљрЃю рЃЮрЃарЃў рЃарЃўрЃфрЃ«рЃЋрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљ
num1 = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃърЃўрЃарЃЋрЃћрЃџрЃў рЃарЃўрЃфрЃ«рЃЋрЃў: "))
num2 = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃЏрЃћрЃЮрЃарЃћ рЃарЃўрЃфрЃ«рЃЋрЃў: "))

