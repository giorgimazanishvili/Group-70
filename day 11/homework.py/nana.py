if num1 > 0 and num2 > 0:
    print("рЃЮрЃарЃўрЃЋрЃћ рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃўрЃљ")
elif (num1 > 0 and num2 <= 0) or (num1 <= 0 and num2 > 0):
    print("рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃћрЃарЃЌрЃў рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃў рЃарЃўрЃфрЃ«рЃЋрЃўрЃљ")
else:
    print("рЃљрЃарЃфрЃћрЃарЃЌрЃў рЃљрЃа рЃљрЃарЃўрЃА рЃЊрЃљрЃЊрЃћрЃЉрЃўрЃЌрЃў")

# 4) рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃА рЃЮрЃа рЃарЃўрЃфрЃ«рЃЋрЃА рЃЊрЃљ рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃљрЃА (+, -, *, /)
# рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃћ рЃерЃћрЃЊрЃћрЃњрЃў рЃерЃћрЃАрЃљрЃЉрЃљрЃЏрЃўрЃАрЃў рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃўрЃЌ.
# рЃЌрЃБ рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃљ рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃўрЃљ (рЃЏрЃљрЃњ 0-рЃА рЃњрЃљрЃДрЃЮрЃцрЃљ рЃљрЃю рЃбрЃћрЃЦрЃАрЃбрЃў рЃљрЃю рЃБрЃфрЃ«рЃЮ рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮ) Рєњ "рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃў рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃљ!"

# рЃЮрЃарЃў рЃарЃўрЃфрЃ«рЃЋрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљ
num1 = float(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃърЃўрЃарЃЋрЃћрЃџрЃў рЃарЃўрЃфрЃ«рЃЋрЃў: "))
num2 = float(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃЏрЃћрЃЮрЃарЃћ рЃарЃўрЃфрЃ«рЃЋрЃў: "))

# рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљ (+, -, *, /)
operation = input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃљ (+, -, *, /): ").strip()

# рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃўрЃА рЃерЃћрЃАрЃарЃБрЃџрЃћрЃЉрЃљ
if operation == "+":
    print("рЃерЃћрЃЊрЃћрЃњрЃў:", num1 + num2)
elif operation == "-":
    print("рЃерЃћрЃЊрЃћрЃњрЃў:", num1 - num2)
elif operation == "*":
    print("рЃерЃћрЃЊрЃћрЃњрЃў:", num1 * num2)
elif operation == "/":
    if num2 == 0:
        print("рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃў рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃљ! рЃюрЃБрЃџрЃќрЃћ рЃњрЃљрЃДрЃЮрЃцрЃљ рЃерЃћрЃБрЃФрЃџрЃћрЃЉрЃћрЃџрЃўрЃљ.")
    else:
        print("рЃерЃћрЃЊрЃћрЃњрЃў:", num1 / num2)
else:
    print("рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃў рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃљ!")

# 5) рЃерЃћрЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЌ рЃарЃћрЃАрЃБрЃарЃАрЃћрЃЉрЃерЃў рЃЕрЃљрЃњрЃЊрЃћрЃЉрЃБрЃџрЃў рЃАрЃБрЃарЃљрЃЌрЃў рЃЊрЃљ рЃЏрЃўрЃА рЃЏрЃўрЃ«рЃћрЃЊрЃЋрЃўрЃЌ рЃЊрЃљрЃгрЃћрЃарЃћрЃЌ рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃљрЃЊ рЃљрЃю рЃърЃарЃћрЃќрЃћрЃюрЃбрЃљрЃфрЃўрЃљрЃерЃў рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЊрЃћрЃЉрЃўрЃА рЃЌрЃљрЃюрЃЏрЃўрЃЏрЃЊрЃћрЃЋрЃарЃЮрЃЉрЃљ рЃЊрЃљ рЃАрЃљрЃЉрЃЮрЃџрЃЮрЃЮ рЃърЃљрЃАрЃБрЃ«рЃў, рЃљрЃАрЃћрЃЋрЃћ рЃарЃЮрЃњрЃЮрЃарЃф рЃюрЃљрЃЏрЃЊрЃЋрЃўрЃџрЃў рЃърЃўрЃЌрЃЮрЃюрЃўрЃА рЃЎрЃЮрЃЊрЃў (рЃерЃћрЃЦрЃЏрЃћрЃюрЃўрЃЌ a,b,c рЃфрЃЋрЃџрЃљрЃЊрЃћрЃЉрЃў, рЃерЃћрЃЦрЃЏрЃћрЃюрЃўрЃЌ result_0, result_1, result_2 рЃфрЃЋрЃџрЃљрЃЊрЃћрЃЉрЃў рЃЊрЃљ рЃерЃћрЃўрЃюрЃљрЃ«рЃћрЃЌ рЃЌрЃўрЃЌрЃЮрЃћрЃБрЃџрЃерЃў рЃерЃћрЃАрЃљрЃЉрЃљрЃЏрЃўрЃАрЃў рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃљ рЃЊрЃљ рЃњрЃљрЃЏрЃЮрЃърЃарЃўрЃюрЃбрЃћрЃЌ).  

# рЃњрЃЌрЃ«рЃЮрЃЋ, рЃљрЃбрЃЋрЃўрЃарЃЌрЃћ рЃљрЃю рЃЊрЃћрЃбрЃљрЃџрЃБрЃарЃљрЃЊ рЃљрЃдрЃгрЃћрЃарЃћ рЃўрЃА **рЃАрЃБрЃарЃљрЃЌрЃў**, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃБрЃюрЃЊрЃљ рЃерЃћрЃЋрЃљрЃцрЃљрЃАрЃЮ.
# рЃерЃћрЃАрЃљрЃЉрЃљрЃЏрЃўрЃАрЃљрЃЊ, рЃерЃћрЃЏрЃЊрЃћрЃњ рЃерЃћрЃЋрЃФрЃџрЃћрЃЉ:

