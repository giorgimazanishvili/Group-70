# 17) რა განსხვავებაა while loopსა და do...while loopს შორის?


# while loop-ში პირობა შემოწმდება ციკლის დაწყებამდე, ხოლო do...while loop-ში პირობა შემოწმდება ციკლის დასრულების შემდეგ, რაც ნიშნავს, რომ do...while loop ყოველთვის შესრულდება მინიმუმ ერთხელ, მაშინ როცა while loop შესაძლოა არც შესრულდეს, თუ პირობა თავიდანვე არ არის მართალი.