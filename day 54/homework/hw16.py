# 16) ახსენით do...while loop.

# do...while loop არის კონტროლის სტრუქტურა, რომელიც საშუალებას აძლევს კოდს განმეორებით შესრულდეს მანამ, სანამ გარკვეული პირობა არ შესრულდება.
# განსხვავებით სხვა loop-ებისგან, do...while loop ყოველთვის ასრულებს კოდის  ბლოკს მინიმუმ ერთხელ, რადგან პირობა შემოწმდება მხოლოდ კოდის შესრულების შემდეგ.
# ეს არის სასარგებლო სიტუაციებში, როდესაც საჭიროა კოდის შესრულება მინიმუმუმ ერთხელ, მაგალითად მომხმარებლისგან მონაცემების მიღებისას.