// 6) დაწერე განსხვავება do...while loopსა და while loopს შორის.

// while loop-ში პირობა შემოწმდება ციკლის დაწყებამდე, ხოლო do...while loop-ში პირობა შემოწმდება ციკლის დასრულების შემდეგ, რაც ნიშნავს, რომ do...while loop ყოველთვის შესრულდება მინიმუმ ერთხელ, მაშინ როცა while loop შეიძლება არ შესრულდეს საერთოდ, თუ პირობა თავიდანვე არ არის მართალი.

// მაგალითი while loop-ის:
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

// მაგალითი do...while loop-ის:
let j = 0;
do {
    console.log(j);
    j++;
} while (j < 5);