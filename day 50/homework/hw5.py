# 5) კომენტარების სახით ახსენი და ჩამოწერე ცვლადის შექმნის რამდენი გზა გვაქვს, და ახსენი თითოეული.

# var, let, და const.

# var - ეს არის ძველი გზა ცვლადის შექმნისთვის JavaScript-ში. მას აქვს ფუნქციური სკოპი, რაც ნიშნავს, რომ თუ ცვლადი შექმნილია ფუნქციის შიგნით, ის ხელმისაწვდომია მხოლოდ იმ ფუნქციის ფარგლებში. თუმცა, თუ ცვლადი შექმნილია გლობალურ დონეზე ან ბლოკის გარეთ, ის ხელმისაწვდომია მთელ სკოპში.

# let - ეს არის ახალი გზა ცვლადის შექმნისთვის, რომელიც Introduced ES6-ში. მას აქვს ბლოკის სკოპი, რაც ნიშნავს, რომ თუ ცვლადი შექმნილია ბლოკის შიგნით (მაგალითად, if-else ან for loop), ის ხელმისაწვდომია მხოლოდ იმ ბლოკის ფარგლებში.

# const - ეს არის კიდევ ერთი ახალი გზა ცვლადის შექმნისთვის, რომელიც ასევე აქვს ბლოკის სკოპი. თუმცა, const-ის გამოყენებით შექმნილი ცვლადები არ შეიძლება შეიცვალოს (immutable), რაც ნიშნავს, რომ once assigned, their value cannot be changed.