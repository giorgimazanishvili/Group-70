# 4) კომენტარების სახით ახსენი რა განსხვავებაა let-ს და const-ს შორის

# let - ეს არის ცვლადი, რომლის მნიშვნელობა შეიძლება შეიცვალოს. მისი გამოყენება რეკომენდებულია მაშინ, როდესაც ჩვენ არ ვიცით ცვლადის საბოლოო მნიშვნელობა და გვინდა, რომ მასზე მუშაობა გავაგრძელოთ.

# const - ეს არის ცვლადი, რომლის მნიშვნელობა არ შეიძლება შეიცვალოს. მისი გამოყენება რეკომენდებულია მაშინ, როდესაც ჩვენ ვიცით, რომ ცვლადის მნიშვნელობა არ უნდა შეიცვალოს და გვინდა, რომ მასზე მუშაობა გავაგრძელოთ.