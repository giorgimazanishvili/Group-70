# 2) კომენტარების სახით ახსენით რაიმენი ცვლადის შექმნის ვარიანტი გვაქვს და განიხილეთ თითოეული

# JavaScript-ში ცვლადის შექმნის რამდენიმე ვარიანტი გვაქვს:

# 1. var - ეს არის ძველი მეთოდი ცვლადების შესაქმნელად. მისი გამოყენება რეკომენდებულია მხოლოდ ძველი კოდის შენარჩუნების მიზნით.

# 2. let - ეს არის თანამედროვე მეთოდი ცვლადების შესაქმნელად, რომელიც საშუალებას აძლევს ცვლადს ჰქონდეს ბლოკის დონეზე ხილვადობა.

# 3. const - ეს არის კიდევ ერთი თანამედროვე მეთოდი, რომელიც გამოიყენება მუდმივი ცვლადების შესაქმნელად. მას არ შეუძლია შეცვალოს თავისი მნიშვნელობა, რაც მას განსაკუთრებით სასარგებლოს ხდის იმ შემთხვევებში, როდესაც ჩვენ არ გვინდა, რომ ცვლადის მნიშვნელობა შეიცვალოს.