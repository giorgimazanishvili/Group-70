# рЃарЃљ рЃЌрЃЦрЃЏрЃљ рЃБрЃюрЃЊрЃљ! рЃЦрЃЋрЃћрЃЏрЃЮрЃЌ рЃљрЃарЃўрЃА Python рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф:

# * рЃЏрЃўрЃўрЃдрЃћрЃЉрЃА рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃњрЃљрЃю рЃарЃўрЃфрЃ«рЃЋрЃА input-рЃўрЃЌ
# * рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃА `for` loop-рЃА
# * рЃЊрЃљ рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА **рЃДрЃЋрЃћрЃџрЃљ рЃарЃўрЃфрЃ«рЃЋрЃА 0-рЃўрЃЊрЃљрЃю рЃўрЃЏ рЃарЃўрЃфрЃ«рЃЋрЃљрЃЏрЃЊрЃћ рЃЕрЃљрЃЌрЃЋрЃџрЃўрЃЌ**:

# python
# рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃњрЃљрЃю рЃарЃўрЃфрЃ«рЃЋрЃўрЃА рЃЏрЃўрЃдрЃћрЃЉрЃљ
number = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃў: "))

# for loop Рђћ 0-рЃўрЃЊрЃљрЃю number-рЃЏрЃЊрЃћ рЃЕрЃљрЃЌрЃЋрЃџрЃўрЃЌ
for i in range(number + 1):  # +1 рЃарЃљрЃЊрЃњрЃљрЃю range рЃЉрЃЮрЃџрЃЮ рЃарЃўрЃфрЃ«рЃЋрЃА рЃљрЃа рЃўрЃЌрЃЋрЃџрЃўрЃА
    print(i)


