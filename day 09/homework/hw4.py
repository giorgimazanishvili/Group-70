# 4. рЃерЃћрЃЦрЃЏрЃћрЃюрЃўрЃЌ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃЏрЃўрЃўрЃдрЃћрЃЉрЃА рЃЏрЃЮрЃЏрЃ«рЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃњрЃљрЃю input-рЃА рЃарЃўрЃ«рЃЋрЃўрЃА рЃАрЃљрЃ«рЃўрЃЌ рЃЊрЃљ рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃћрЃЌ рЃљрЃЏ рЃарЃўрЃфрЃ«рЃЋрЃљрЃЏрЃЊрЃћ рЃДрЃЋрЃћрЃџрЃљ рЃарЃўрЃфрЃ«рЃЋрЃў рЃЌрЃљрЃюрЃЏрЃўрЃЏрЃЊрЃћрЃЋрЃарЃЮрЃЉрЃўрЃЌ. (For loop)

# рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃњрЃљрЃю рЃарЃўрЃфрЃ«рЃЋрЃўрЃА рЃЏрЃўрЃдрЃћрЃЉрЃљ
num = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃў: "))

# рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃЋрЃљ 0-рЃЊрЃљрЃю num-рЃўрЃА рЃЕрЃљрЃЌрЃЋрЃџрЃўрЃЌ (рЃљрЃю num-рЃўрЃА рЃњрЃљрЃарЃћрЃерЃћ, рЃАрЃБрЃарЃЋрЃўрЃџрЃўрЃА рЃЏрЃўрЃ«рЃћрЃЊрЃЋрЃўрЃЌ)
for i in range(num + 1):
    print(i)


# ­ЪЊї рЃерЃћрЃюрЃўрЃерЃЋрЃюрЃљ:

# int(input(...)) рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃўрЃЏрЃўрЃАрЃљрЃЌрЃЋрЃўрЃА, рЃарЃЮрЃЏ input-рЃА рЃарЃўрЃфрЃ«рЃЋрЃљрЃЊ рЃњрЃљрЃарЃЊрЃљрЃЋрЃЦрЃЏрЃюрЃљрЃЌ (explicit conversion).

# range(num + 1) рЃфрЃўрЃЎрЃџрЃА рЃљрЃбрЃљрЃарЃћрЃЉрЃА 0-рЃЊрЃљрЃю num рЃЕрЃљрЃЌрЃЋрЃџрЃўрЃЌ.

# рЃЌрЃБ рЃњрЃўрЃюрЃЊрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ num-рЃЏрЃЊрЃћ (рЃњрЃљрЃЏрЃЮрЃЎрЃџрЃћрЃЉрЃўрЃЌ), рЃЏрЃљрЃерЃўрЃю рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћ рЃБрЃЉрЃарЃљрЃџрЃЮрЃЊ range(num).


# 0-рЃЊрЃљрЃю num-рЃЏрЃЊрЃћ (num рЃњрЃљрЃЏрЃЮрЃЎрЃџрЃћрЃЉрЃўрЃЌ)
for i in range(num):
    print(i)