# 2. рЃљрЃ«рЃАрЃћрЃюрЃўрЃЌ рЃарЃљ рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃљрЃљ implicit рЃЊрЃљ explicit datatype comversion-рЃерЃў.

# Implicit рЃЊрЃљ Explicit рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃЌрЃљ рЃбрЃўрЃърЃўрЃА рЃЎрЃЮрЃюрЃЋрЃћрЃарЃбрЃљрЃфрЃўрЃћрЃЉрЃА рЃерЃЮрЃарЃўрЃА рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃљ рЃФрЃўрЃарЃўрЃЌрЃљрЃЊрЃљрЃЊ рЃўрЃЏрЃљрЃерЃў рЃЏрЃЊрЃњрЃЮрЃЏрЃљрЃарЃћрЃЮрЃЉрЃА, рЃЌрЃБ рЃЋрЃўрЃю рЃљрЃ«рЃЊрЃћрЃюрЃА рЃбрЃўрЃърЃћрЃЉрЃўрЃА рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃљрЃА Рђћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃБрЃџрЃў рЃћрЃюрЃљ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ рЃЌрЃБ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃАрЃбрЃў рЃ«рЃћрЃџрЃўрЃЌ.

# ­Ъћ╣ Implicit Type Conversion (рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃў рЃЎрЃЮрЃюрЃЋрЃћрЃарЃбрЃљрЃфрЃўрЃљ)
# рЃљрЃАрЃћрЃЋрЃћ рЃфрЃюрЃЮрЃЉрЃўрЃџрЃўрЃљ рЃарЃЮрЃњрЃЮрЃарЃф type coercion.
# рЃћрЃА рЃљрЃарЃўрЃА рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃўрЃА рЃбрЃўрЃърЃўрЃА рЃерЃћрЃфрЃЋрЃџрЃљ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ, рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃА рЃЏрЃўрЃћрЃа, рЃарЃЮрЃфрЃљ рЃАрЃљрЃГрЃўрЃарЃЮ рЃ«рЃЊрЃћрЃЉрЃљ.

# ­ЪЊї рЃ«рЃљрЃАрЃўрЃљрЃЌрЃЊрЃћрЃЉрЃљ:

# рЃ«рЃЊрЃћрЃЉрЃљ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃА рЃЏрЃўрЃћрЃа.

# рЃљрЃа рЃАрЃљрЃГрЃўрЃарЃЮрЃћрЃЉрЃА рЃЊрЃљрЃЏрЃљрЃбрЃћрЃЉрЃўрЃЌ рЃЎрЃЮрЃЊрЃА.

# рЃЕрЃЋрЃћрЃБрЃџрЃћрЃЉрЃарЃўрЃЋ рЃ«рЃЊрЃћрЃЉрЃљ рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃћрЃЉрЃўрЃА рЃљрЃарЃљрЃЎрЃЮрЃЏрЃцрЃџрЃўрЃЦрЃбрЃБрЃарЃў рЃбрЃўрЃърЃћрЃЉрЃўрЃЊрЃљрЃю рЃБрЃцрЃарЃЮ РђърЃќрЃћрЃЊрЃљРђю рЃбрЃўрЃърЃћрЃЉрЃќрЃћ (рЃЏрЃљрЃњ. int Рєњ float).

# ­ЪЊї рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў (Python):

# python
# Copy
# Edit
x = 10       # integer
y = 2.5      # float
z = x + y    # Python рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃўрЃА x-рЃА float-рЃљрЃЊ
print(z)     # 12.5 (float)

# ­Ъћ╣ Explicit Type Conversion (рЃ«рЃћрЃџрЃўрЃЌ рЃЎрЃЮрЃюрЃЋрЃћрЃарЃбрЃљрЃфрЃўрЃљ)
# рЃљрЃАрЃћрЃЋрЃћ рЃфрЃюрЃЮрЃЉрЃўрЃџрЃўрЃљ рЃарЃЮрЃњрЃЮрЃарЃф type casting.
# рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃАрЃбрЃў рЃАрЃърЃћрЃфрЃўрЃљрЃџрЃБрЃарЃљрЃЊ рЃЏрЃўрЃБрЃЌрЃўрЃЌрЃћрЃЉрЃА, рЃарЃЮрЃЏ рЃБрЃюрЃЊрЃљ рЃЏрЃЮрЃ«рЃЊрЃћрЃА рЃбрЃўрЃърЃўрЃА рЃерЃћрЃфрЃЋрЃџрЃљ.

# ­ЪЊї рЃ«рЃљрЃАрЃўрЃљрЃЌрЃЊрЃћрЃЉрЃљ:

# рЃљрЃБрЃфрЃўрЃџрЃћрЃЉрЃћрЃџрЃўрЃљ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃАрЃбрЃўрЃА рЃЏрЃўрЃћрЃа рЃЎрЃЮрЃЊрЃўрЃА рЃЊрЃљрЃЏрЃљрЃбрЃћрЃЉрЃљ.

# рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ int(), float(), str() рЃЊрЃљ рЃАрЃ«рЃЋрЃљ рЃцрЃБрЃюрЃЦрЃфрЃўрЃћрЃЉрЃў.

# рЃАрЃљрЃГрЃўрЃарЃЮрЃљ, рЃарЃЮрЃфрЃљ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃљ рЃерЃћрЃБрЃФрЃџрЃћрЃЉрЃћрЃџрЃўрЃљ рЃљрЃю рЃљрЃарЃљрЃАрЃљрЃАрЃБрЃарЃЋрЃћрЃџрЃўрЃљ.

# ­ЪЊї рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў (Python):

# python
# Copy
# Edit
a = "5"
b = 10
c = int(a) + b   # рЃљрЃЦ рЃ«рЃЊрЃћрЃЉрЃљ string Рєњ int рЃЎрЃЮрЃюрЃЋрЃћрЃарЃбрЃљрЃфрЃўрЃљ рЃ«рЃћрЃџрЃўрЃЌ
print(c)         # 15

# ­ЪДЕ рЃерЃћрЃ»рЃљрЃЏрЃћрЃЉрЃљ:

# рЃърЃљрЃарЃљрЃЏрЃћрЃбрЃарЃў	Implicit Conversion	Explicit Conversion
# рЃЋрЃўрЃю рЃљрЃ«рЃЮрЃарЃфрЃўрЃћрЃџрЃћрЃЉрЃА	рЃћрЃюрЃљ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ	рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃАрЃбрЃў
# рЃАрЃљрЃГрЃўрЃарЃЮрЃћрЃЉрЃА рЃЎрЃЮрЃЊрЃА	рЃљрЃарЃљ	рЃЎрЃў
# рЃарЃўрЃАрЃЎрЃћрЃЉрЃў	рЃюрЃљрЃЎрЃџрЃћрЃЉрЃў	рЃЏрЃћрЃбрЃў (рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ рЃЏрЃЮрЃ«рЃЊрЃћрЃА рЃерЃћрЃфрЃЊрЃЮрЃЏрЃљ)
# рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў	int + float Рєњ float	int("123"), float("3.14")

# рЃЌрЃБ рЃњрЃўрЃюрЃЊрЃљ, рЃерЃћрЃЏрЃўрЃФрЃџрЃўрЃљ рЃњрЃљрЃЋрЃљрЃЎрЃћрЃЌрЃЮ рЃАрЃЦрЃћрЃЏрЃљ рЃљрЃю рЃњрЃарЃљрЃцрЃўрЃЎрЃБрЃџрЃљрЃЊ рЃљрЃњрЃўрЃ«рЃАрЃюрЃљ рЃАрЃ«рЃЋрЃљрЃЮрЃЉрЃљ.




