#  2. рЃљрЃ«рЃАрЃћрЃюрЃўрЃЌ рЃарЃљ рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃљрЃљ implicit рЃЊрЃљ explicit datatype comversion-рЃерЃў.

# рЃЏрЃерЃЋрЃћрЃюрЃўрЃћрЃарЃў рЃЎрЃўрЃЌрЃ«рЃЋрЃљ! рЃљрЃў рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃљ **implicit** (рЃљрЃарЃљрЃърЃўрЃарЃЊрЃљрЃърЃўрЃарЃў) рЃЊрЃљ **explicit** (рЃърЃўрЃарЃЊрЃљрЃърЃўрЃарЃў) рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃЌрЃљ рЃбрЃўрЃърЃћрЃЉрЃўрЃА рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃћрЃЉрЃА рЃерЃЮрЃарЃўрЃА Python-рЃерЃў:


# РюЁ **1. Implicit Type Conversion (рЃљрЃарЃљрЃърЃўрЃарЃЊрЃљрЃърЃўрЃарЃў рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃљ)**

# ­ЪЊї рЃћрЃА рЃљрЃарЃўрЃА **рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃў** рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃАрЃљрЃф Python рЃљрЃЎрЃћрЃЌрЃћрЃЉрЃА **рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃЌрЃљ рЃбрЃўрЃърЃћрЃЉрЃА рЃерЃЮрЃарЃўрЃА**, рЃарЃЮрЃфрЃљ рЃАрЃљрЃГрЃўрЃарЃЮ рЃ«рЃЊрЃћрЃЉрЃљ рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃўрЃА рЃАрЃгрЃЮрЃарЃљрЃЊ рЃерЃћрЃАрЃарЃБрЃџрЃћрЃЉрЃљ.

# РќХ№ИЈ рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў:

# python:

x = 5        # int
y = 2.5      # float

result = x + y  # Python рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃўрЃА x-рЃА float рЃбрЃўрЃърЃљрЃЊ
print(result)   # рЃњрЃљрЃЏрЃЮрЃўрЃбрЃљрЃюрЃА 7.5 (float)


# ­Ъћ╣ рЃљрЃЦ `int` рЃбрЃўрЃърЃўрЃА 5 рЃњрЃљрЃарЃЊрЃљрЃўрЃЦрЃЏрЃюрЃљ `float`-рЃљрЃЊ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ, рЃарЃЮрЃЏ `float`-рЃЌрЃљрЃю рЃерЃћрЃћрЃФрЃџрЃЮрЃА рЃЏрЃБрЃерЃљрЃЮрЃЉрЃљ.



### РюЁ **2. Explicit Type Conversion (рЃърЃўрЃарЃЊрЃљрЃърЃўрЃарЃў рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃљ)**

# ­ЪЊї рЃћрЃА рЃљрЃарЃўрЃА рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃљ, рЃарЃЮрЃфрЃљ **рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃЌрЃљрЃЋрЃљрЃЊ рЃБрЃЌрЃўрЃЌрЃћрЃЉрЃА**, рЃарЃЮрЃЏрЃћрЃџ рЃбрЃўрЃърЃљрЃЊ рЃБрЃюрЃЊрЃљ рЃњрЃљрЃарЃЊрЃљрЃўрЃЦрЃЏрЃюрЃљрЃА рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃў, рЃерЃћрЃАрЃљрЃЉрЃљрЃЏрЃўрЃАрЃў рЃцрЃБрЃюрЃЦрЃфрЃўрЃўрЃА рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃўрЃЌ Рђћ рЃЏрЃљрЃњ. `int()`, `float()`, `str()` рЃЊрЃљ рЃАрЃ«рЃЋ.

# РќХ№ИЈ рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў:

# python:
x = "10"
y = 5

result = int(x) + y  # рЃљрЃЦ рЃЕрЃЋрЃћрЃю рЃЋрЃљрЃЏрЃЉрЃЮрЃЉрЃЌ: рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃћрЃю 'x' рЃАрЃбрЃарЃўрЃюрЃњрЃўрЃЊрЃљрЃю рЃарЃўрЃфрЃ«рЃЋрЃљрЃЊ
print(result)        # рЃњрЃљрЃЏрЃЮрЃўрЃбрЃљрЃюрЃА 15

# ­Ъћ╣ `int(x)` рЃљрЃЦ рЃљрЃарЃўрЃА **explicit conversion**, рЃарЃљрЃЊрЃњрЃљрЃю рЃЕрЃЋрЃћрЃю рЃЌрЃљрЃЋрЃљрЃЊ рЃЋрЃЌрЃ«рЃЮрЃЋрЃћрЃЌ рЃњрЃљрЃарЃЊрЃљрЃЦрЃЏрЃюрЃљ.


### ­ЪЊї рЃФрЃўрЃарЃўрЃЌрЃљрЃЊрЃў рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃљ рЃЏрЃЮрЃЎрЃџрЃћрЃЊ:

# | рЃЏрЃљрЃ«рЃљрЃАрЃўрЃљрЃЌрЃћрЃЉрЃћрЃџрЃў | Implicit Conversion               | Explicit Conversion                     |
# | ------------- | --------------------------------- | --------------------------------------- |
# | рЃЋрЃўрЃю рЃљрЃЎрЃћрЃЌрЃћрЃЉрЃА?  | Python рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ                | рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃАрЃбрЃў рЃ«рЃћрЃџрЃўрЃЌ                       |
# | рЃБрЃАрЃљрЃцрЃарЃЌрЃ«рЃЮрЃћрЃЉрЃљрЃљ? | рЃБрЃАрЃљрЃцрЃарЃЌрЃ«рЃЮрЃљ, рЃарЃЮрЃфрЃљ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ рЃ«рЃЊрЃћрЃЉрЃљ | рЃАрЃљрЃГрЃўрЃарЃЮрЃћрЃЉрЃА рЃДрЃБрЃарЃљрЃЊрЃдрЃћрЃЉрЃљрЃА Рђћ рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ рЃерЃћрЃфрЃЊрЃЮрЃЏрЃљ |
# | рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў      | `int + float Рєњ float`             | `int("123")` Рєњ 123                      |

# ---

# рЃЌрЃБ рЃњрЃўрЃюрЃЊрЃљ рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃћрЃЉрЃў рЃерЃћрЃфрЃЊрЃЮрЃЏрЃћрЃЉрЃќрЃћ рЃљрЃю рЃБрЃцрЃарЃЮ рЃарЃЌрЃБрЃџрЃў рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃћрЃЉрЃўрЃф рЃњрЃљрЃЕрЃЋрЃћрЃюрЃЮ, рЃАрЃўрЃљрЃЏрЃЮрЃЋрЃюрЃћрЃЉрЃўрЃЌ рЃЊрЃљрЃњрЃћрЃ«рЃЏрЃљрЃарЃћрЃЉрЃў!
