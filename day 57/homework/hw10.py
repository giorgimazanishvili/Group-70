# 10) ახსენით რას აკეთებს :focus()

# :focus() სელექტორი გამოიყენება CSS-ში იმისთვის, რომ დაადგინოს, თუ რომელი ელემენტი აქვს ფოკუსი (მაგალითად, როდესაც მომხმარებელი კლავს TAB ღილაკს და გადადის ფორმის ველებს შორის). ეს სელექტორი ხშირად გამოიყენება ფორმის ელემენტებზე, როგორიცაა input, textarea და button, რათა სტილიზაცია გაუკეთოს მათ, როდესაც ისინი ფოკუსში არიან. 

