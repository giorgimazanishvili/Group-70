# 2) ახსენი რას აკეთებს :hover().

# :hover ფსევდო კლასი გამოიყენება CSS-ში იმისთვის, რომ დაადგინოს ელემენტის სტილი მაშინ, როდესაც მაუსის კურსორი იმყოფება ამ ელემენტზე.
# მაგალითად, თუ გვსურს რომ ღილაკის ფერი შეიცვალოს როდესაც მომხმარებელი მაუსს ღილაკზე მიიყვანს, შეგვიძლია გამოვიყენოთ :hover კლასი.

# მაგალითი:

# button:hover {
#     background-color: blue; /* ღილაკის ფერი შეიცვლება ლურჯად როდესაც მაუსი ღილაკზე იქნება */
#     color: white; /* ტექსტის ფერი შეიცვლება თეთრად */