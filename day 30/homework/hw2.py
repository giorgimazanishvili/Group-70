# 2) ახსენით რას შვება color, background-color, font-size, text-align, border.

# color - განსაზღვრავს ტექსტის ფერს.
# background-color - განსაზღვრავს ელემენტის ფონური ფერს.
# font-size - განსაზღვრავს ტექსტის ზომას.
# text-align - განსაზღვრავს ტექსტის განლაგებას (მაგ. მარცხნივ, მარჯვნივ, ცენტრში).
# border - განსაზღვრავს ელემენტის საზღვრების სტილს, სისქეს და ფერს.
