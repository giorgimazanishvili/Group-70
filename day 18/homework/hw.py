# 1) აჰსენი მოკლედ, რას ნიშნავს semantic tag და რისთვის არის საჭირო HTML-ში.

# Semantic tag არის HTML-ში ელემენტების ტიპი, რომელიც შეიცავს გარკვეულ კონტექსტს ან მნიშვნელობას. სხვა სიტყვებით რომ ვთქვათ, ეს ტეგები უფრო ლოგიკურია და უფრო ზუსტად ასახავს ელემენტის როლს გვერდზე. მაგალითად, <header>, <footer>, <article>, <section>, <nav> და სხვა მსგავსი ტეგები — ყველა მათგანი აზროვნებასა და სტრუქტურას ემსახურება, არა მხოლოდ ვიზუალურ წარმოდგენას.

# მაგალითი:

# <header> — გვერდის ზედა ნაწილი.

# <nav> — ნავიგაციისთვის.

# <article> — სტატია.

# <footer> — გვერდის ქვედა ნაწილი.