# 1) ახსენი რა არის semanticTag-ები 

# 1) „სემანტიკა“ ნიშნავს მნიშვნელობას. როცა ვამბობთ "სემანტიკური თეგი", ვგულისხმობთ HTML ელემენტს, რომელსაც აქვს გარკვეული მნიშვნელობა — მაგალითად, „ეს არის მენიუ“, „ეს არის სტატია“, „ეს არის გვერდის სათაური“ და ა.შ.

