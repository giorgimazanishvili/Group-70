# 1) კომენტარების სახით ახსენით  რა არის ობიექტი.

# ობიექტი — ეს არის არსება/მონაცვლე ერთეული, რომელსაც აქვს მდგომარეობა (მონაცემები) და ქცევა (ფუნქციები).
# მარტივად რომ ვთქვათ, ობიექტი არის რაღაც "ნივთი", რომელსაც პროგრამაში ვიყენებთ.
# მაგალითად, ადამიანი შეიძლება იყოს ობიექტი: 
# მას აქვს თვისებები (სახელი, ასაკი, სიმაღლე) — ეს მისმა მონაცემებმა (ატრიბუტებმა) წარმოადგენს.
# ასევე შეუძლია მოქმედებები (ლაპარაკი, სიარული, ძილი) — ეს მის მეთოდებად ითვლება.
# ობიექტი იქმნება კლასის საფუძველზე, ხოლო კლასი არის შაბლონი/ნიმუში, რომლის მიხედვითაც მზადდება ობიექტები.