# 3) მიუწერეთ ამ მნიშვნელობებს განმარტებები (რა რას აკეთებს).

# 1. მონაცემების ფორმატირება - ეს პროცესი მოიცავს მონაცემების კონკრეტულ ფორმატში გადაყვანას, რათა ისინი უფრო ადვილად წვდომადი და გასაგები გახდეს.

# 2. მონაცემების გარდაქმნა - ეს პროცესი მოიცავს მონაცემების ფორმის ან სტრუქტურის შეცვლას, რათა ისინი უფრო შესაბამისი გახდეს კონკრეტულ საჭიროებებთან.

# 3. მონაცემების სტრუქტურის შეცვლა - ეს პროცესი მოიცავს მონაცემების ორგანიზაციის ცვლილებას, რათა ისინი უფრო ეფექტურად იქნას გამოყენებული ან გაანალიზებული.

# 4. მონაცემების ტიპის შეცვლა - ეს პროცესი მოიცავს მონაცემების ტიპის ცვლილებას, მაგალითად, ტექსტის რიცხვად გადაქცევას ან პირიქით.