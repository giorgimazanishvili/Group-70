# 3) კომენტარების სახით ახსენი რას აკეთბს font-family, font-size, font-weight, font-style

# font-family - განსაზღვრავს ტექსტის შრიფტის სახეობას. მაგალითად: font-family: Arial, sans-serif;
# font-size - განსაზღვრავს ტექსტის ზომას. მაგალითად: font-size: 16px;
# font-weight - განსაზღვრავს ტექსტის სიმძიმეს (სქელობას). მაგალითად: font-weight: bold;
# font-style - განსაზღვრავს ტექსტის სტილს (მაგალითად, მეილური). მაგალითად: font-style: italic;